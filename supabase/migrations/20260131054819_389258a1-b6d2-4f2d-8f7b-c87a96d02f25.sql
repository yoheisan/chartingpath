The Hammer and Hanging Man share identical structures—a small body at the top of the range with a long lower shadow—but their meaning depends entirely on context. The Hammer is bullish at bottoms, while the Hanging Man is bearish at tops.

## Pattern Structure

### Physical Characteristics

Both patterns feature:
- Small real body at upper end of range
- Long lower shadow (at least 2x body length)
- Little or no upper shadow
- Body can be bullish or bearish

### The Critical Difference: Context

**Hammer (Bullish):**
- Appears after downtrend
- At support levels
- Signals buying interest

**Hanging Man (Bearish):**
- Appears after uptrend
- At resistance levels
- Signals selling pressure despite close

## Hammer Pattern (Bullish)

### Psychology

**During Formation:**
- Price opens and sells off sharply
- Buyers enter at lower levels
- Price recovers to close near high
- Lower shadow shows buying support

**The Message:**
- Sellers pushed lower but couldn''t hold
- Buyers defended and recovered
- Potential reversal forming

### Trading the Hammer

**Entry:**
- Wait for bullish confirmation (next candle closes above hammer high)
- Don''t trade hammer alone

**Stop-Loss:**
- Below the hammer''s low (the shadow)
- This is the proven support level

**Target:**
- Prior resistance levels
- Fibonacci retracements

### Ideal Hammer Characteristics
- Very long lower shadow (2-3x body)
- Small body at top of range
- At significant support
- After extended decline
- Volume spike on formation

## Hanging Man Pattern (Bearish)

