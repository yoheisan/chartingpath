The Doji is one of the most recognized candlestick patterns—a single candle where the open and close are virtually equal. This pattern signals market indecision and often appears at reversal points, making it a valuable tool for identifying potential trend changes.

## Understanding the Doji

### What Creates a Doji

A Doji forms when:
- Opening price ≈ Closing price
- Neither buyers nor sellers win the session
- Market is in equilibrium
- Often signals exhaustion

### Pattern Psychology

**During Doji Formation:**
- Price may move significantly during session
- Bulls and bears battle throughout
- Session ends with stalemate
- Neither side gains advantage

**Signal Interpretation:**
- In uptrend: Bulls losing momentum
- In downtrend: Bears losing momentum
- At support/resistance: Reversal potential
- In consolidation: Continued indecision

## Doji Variations

### Standard Doji
- Small or no body
- Upper and lower shadows
- Shows general indecision

### Long-Legged Doji
- Very long upper and lower shadows
- Significant intraday volatility
- Strong indecision signal
- Often appears at major turning points

### Dragonfly Doji
- No upper shadow (or very small)
- Long lower shadow
- Open, high, and close at same level
- Bullish at bottoms

### Gravestone Doji
- No lower shadow (or very small)
- Long upper shadow
- Open, low, and close at same level
- Bearish at tops

### Four Price Doji
- No shadows at all
- Open = High = Low = Close
- Extremely rare
- Complete market inactivity

## Trading Doji Patterns

### Context Is Critical

**Doji in Uptrend:**
- Potential bearish reversal
- Bulls may be exhausting
- Watch for confirmation

**Doji in Downtrend:**
- Potential bullish reversal
- Bears may be exhausting
- Watch for confirmation

**Doji in Range:**
- Continued consolidation
- Less significance
- Wait for breakout

### Confirmation Requirements

**Why Confirmation Matters:**
- Doji only shows indecision
- Next candle determines direction
- Never trade doji alone

**Bullish Confirmation:**
- Next candle closes bullish
- Closes above doji high
- Volume supports move

**Bearish Confirmation:**
- Next candle closes bearish
- Closes below doji low
- Volume supports move

### Entry Strategies

**After Bullish Confirmation:**
- Enter long on confirmation close
- Stop below doji low

**After Bearish Confirmation:**
- Enter short on confirmation close
- Stop above doji high

