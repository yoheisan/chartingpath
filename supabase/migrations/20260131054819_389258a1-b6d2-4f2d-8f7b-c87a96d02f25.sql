**On Bulls:**
- Initial euphoria from gap up
- Growing concern as price falls
- Trapped at higher prices
- May become sellers

**On Bears:**
- See rejection at new highs
- Gain confidence from intraday reversal
- Add short positions
- Momentum shifts to selling

