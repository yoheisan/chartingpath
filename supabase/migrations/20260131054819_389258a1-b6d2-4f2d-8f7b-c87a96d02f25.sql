The pattern captures the moment sellers take control:

**First Candle:**
- Bullish continuation of uptrend
- Strong buying into the close
- Bulls confident of further gains

**Second Candle:**
- Opens above first candle''s close (gaps up)
- Initially bullish momentum continues
- Sellers emerge and overwhelm buyers
- Closes below midpoint of first candle
- Dramatic intraday reversal

