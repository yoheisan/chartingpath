The Three White Soldiers is a powerful bullish reversal pattern consisting of three consecutive long-bodied bullish candles. This pattern represents strong buying momentum and often signals the beginning of a sustained uptrend when appearing at market bottoms.

## Pattern Psychology

### Why Three White Soldiers Is Significant

The pattern demonstrates progressive bullish conviction:

**First Soldier:**
- Initial buying interest emerges
- Closes near the high
- Sets the reversal tone

**Second Soldier:**
- Buyers remain committed
- Opens within first body, closes higher
- Confirms buying continuation

**Third Soldier:**
- Bulls maintain control
- Another strong close
- Triple confirmation of reversal

### The Momentum Shift

This pattern shows complete sentiment change:
- Sellers lose control after three failures
- Each close higher demoralizes bears
- Momentum clearly shifts to buyers
- Often triggers short covering cascade

## Technical Requirements

### Required Elements

**Three Consecutive Bullish Candles:**
- All three candles must be bullish (close > open)
- Each candle should be long-bodied
- Bodies should be at least 2/3 of total range

**Progressive Closes:**
- Each candle closes higher than previous
- No significant overlap (opening within prior body acceptable)
- Steady progression upward

**Wick Characteristics:**
- Small or no upper wicks (ideal)
- Small lower wicks acceptable
- Long wicks reduce reliability

### Context Requirements

**Prior Trend:**
- Must appear after downtrend
- At support levels increases significance
- After consolidation also valid

## Trading the Three White Soldiers

### Entry Strategies

**Aggressive Entry:**
- Enter after third soldier completes
- Confirmation comes from pattern itself
- Stop below pattern low

**Conservative Entry:**
- Wait for fourth candle confirmation
- Enter on continuation close
- May miss some of the move

### Stop-Loss Placement

**Conservative Stop:**
- Below the low of the first soldier
- Accounts for full pattern invalidation

**Aggressive Stop:**
- Below the low of the third soldier
- Tighter risk, higher failure rate

### Price Target

**Initial Target:**
- Measure pattern height (low to high)
- Project upward from breakout

**Extended Targets:**
