## Chart Pattern Strategies

Strategies based on recognizable price formations that reflect underlying supply/demand dynamics.

### 29. Head and Shoulders
**Level:** Intermediate | **Timeframe:** Daily to weekly

Classic reversal pattern with three peaks—the middle peak (head) higher than two shoulders. Signals exhaustion of buying pressure.

**Entry:** Short on neckline break with volume increase. For conservative entry, wait for a retest of the broken neckline as resistance.
**Exit:** Target equals head-to-neckline distance projected downward. Stop above right shoulder.
**Risk Management:** More reliable on higher timeframes. Declining volume on right shoulder confirms weakening buying pressure. Inverse H&S signals bullish reversal at bottoms.

📖 **Further Reading:** [Head and Shoulders Pattern: Complete Trading Guide](/blog/head-and-shoulders) | [Head and Shoulders Pattern Trading: Reversal Mastery](/blog/head-shoulders-trading)

### 30. Double Top/Bottom
**Level:** Beginner | **Timeframe:** Any

Two-peak or two-trough reversal patterns. The market tests a level twice and fails, signaling exhaustion.

**Entry:** Double top: short on break below the valley between peaks. Double bottom: buy on break above the peak between troughs. Confirm with volume.
**Exit:** Target equals pattern height projected from breakout.
**Risk Management:** Second peak/trough should be within 3% of the first. Wider spacing = stronger pattern.

📖 **Further Reading:** [Double Top and Double Bottom Patterns](/blog/double-top-bottom)

### 31. Triangle Breakouts
**Level:** Intermediate | **Timeframe:** Any

Ascending, descending, and symmetrical triangles compress volatility until a breakout occurs.

**Entry:** Ascending: buy above flat resistance. Descending: short below flat support. Symmetrical: trade in direction of prior trend. Enter on close beyond trendline with volume.
**Exit:** Target equals widest part of triangle projected from breakout.
**Risk Management:** Breakout should occur in final 1/3 of the triangle. Early breakouts are more powerful.

📖 **Further Reading:** [Triangle Patterns: Comprehensive Trading Guide](/blog/triangle-patterns) | [Ascending Triangle](/blog/ascending-triangle) | [Descending Triangle](/blog/descending-triangle) | [Symmetrical Triangle](/blog/symmetrical-triangle)

### 32. Flags and Pennants
**Level:** Intermediate | **Timeframe:** Intraday to weekly

Continuation patterns—brief consolidations within strong trends. Flags are parallel channels; pennants are small symmetrical triangles.

**Entry:** Enter on breakout from the pattern in the direction of the prior trend. Volume should contract during formation and expand on breakout.
**Exit:** Target equals the "flagpole" (sharp move preceding the pattern) projected from breakout.
**Risk Management:** Tight formations (4-8 bars) are more reliable. The flagpole should be steep (45°+).

📖 **Further Reading:** [Flags and Pennants: Trend Continuation Patterns](/blog/flags-pennants-trading) | [Flags and Pennants: Continuation Pattern Mastery](/blog/flag-pennant-patterns)

### 33. Cup and Handle
**Level:** Intermediate | **Timeframe:** Weekly to monthly

Bullish continuation pattern shaped like a tea cup. Popularized by William O''Neil.

**Entry:** Buy on breakout above the handle''s upper trendline with volume 50%+ above average.
**Exit:** Minimum target: cup depth projected upward. Many lead to 20%+ gains in growth stocks.
**Risk Management:** Cup depth 12-35%. Handle should drift down no more than 12-15% from rim. Handle in upper half of cup.

📖 **Further Reading:** [Cup and Handle Pattern: Growth Stock Strategy](/blog/cup-and-handle)

### 34. Wedge Patterns
**Level:** Intermediate | **Timeframe:** Daily to weekly

Rising wedges are bearish; falling wedges are bullish. Converging trendlines represent gradual exhaustion of the prevailing move.

**Entry:** Rising wedge: short on break below lower trendline. Falling wedge: buy on break above upper trendline. Require at least 5 touches.
**Exit:** Target the widest part of the wedge projected from breakout.
**Risk Management:** 65-70% reliability as reversal patterns. Volume should decrease throughout formation.

📖 **Further Reading:** [Rising Wedge Pattern](/blog/rising-wedge) | [Falling Wedge Pattern](/blog/falling-wedge)

### 35. Rectangle Consolidation
**Level:** Beginner | **Timeframe:** Any

Range-bound trading between clear horizontal support and resistance before a breakout.

**Entry:** Trade the range: buy at support, short at resistance. Or wait for breakout with volume confirmation.
**Exit:** Range: target opposite boundary. Breakout: target rectangle height projected from breakout.
**Risk Management:** More touches = more reliable levels. Breakout direction often follows prior trend.

📖 **Further Reading:** [Rectangle Pattern: Trading the Range](/blog/rectangle-pattern)

### 36. Diamond Pattern
**Level:** Advanced | **Timeframe:** Daily to weekly

A rare reversal pattern—broadening pattern followed by symmetrical triangle, forming a diamond shape at major tops and bottoms.

**Entry:** Short on break below lower right trendline (at tops). Buy on break above upper right trendline (at bottoms).
**Exit:** Target equals diamond height projected from breakout.
**Risk Management:** Rare pattern—don''t force identification. Most reliable on daily or weekly charts. Confirm with volume.

📖 **Further Reading:** [Diamond Top Pattern](/blog/diamond-top) | [Diamond Bottom Pattern](/blog/diamond-bottom)

### 37. Broadening Formation
**Level:** Advanced | **Timeframe:** Daily to weekly

Megaphone pattern—expanding trendlines with higher highs and lower lows. Reflects increasing volatility and market disagreement.

**Entry:** Trade reversals at the trendlines: short at upper, buy at lower. Or enter on breakout beyond either trendline.
**Exit:** Target the opposite trendline or the widest point projected from breakout.
**Risk Management:** One of the hardest patterns to trade. Use tight stops and reduced position size. Often appears at major market tops.

📖 **Further Reading:** [Broadening Formation Pattern](/blog/broadening-formation)

### 38. Bump and Run Reversal
**Level:** Advanced | **Timeframe:** Daily to weekly

Three phases: gradual lead-in trend, steep bump (speculative excess), and run (reversal). Identifies speculative excess before a crash.

**Entry:** Short when price breaks below the lead-in trendline after the bump completes. Bump should be at least 2x the lead-in angle.
**Exit:** Target the starting point of the lead-in phase.
**Risk Management:** Lead-in phase should last at least 1 month. Stop above the bump''s highest point.

📖 **Further Reading:** [Bump and Run Reversal Pattern](/blog/bump-and-run)

### 39. Channel Trading
**Level:** Intermediate | **Timeframe:** Any

Trade between parallel trendlines (ascending, descending, or horizontal). Channels provide clear, repetitive opportunities with defined risk.

**Entry:** Buy at lower channel line, short at upper. Enter when price touches boundary and shows reversal candle confirmation.
**Exit:** Target the opposite channel line. On breakout, project channel width for measured move.
**Risk Management:** Need at least 4 touches (2 per line). Place stops just outside the channel.

📖 **Further Reading:** [Channel Trading: Parallel Line Trading](/blog/channel-trading) | [Channel Trading Patterns](/blog/channel-patterns)

### 40. Harmonic Patterns
**Level:** Expert | **Timeframe:** Any

Fibonacci-based geometric patterns (Gartley, Butterfly, Bat, Crab) that predict reversal zones with mathematical precision.

**Entry:** Enter at the pattern completion point (Point D) in the Potential Reversal Zone. Confirm with candlestick reversal or RSI divergence.
**Exit:** Target 38.2% and 61.8% retracements of the CD leg. Move stop to breakeven after first target.
**Risk Management:** Strict Fibonacci requirements must be met. Invalidation when price moves beyond PRZ by more than the XA leg.

📖 **Further Reading:** [Harmonic Patterns: Fibonacci-Based Trading](/blog/harmonic-patterns)

