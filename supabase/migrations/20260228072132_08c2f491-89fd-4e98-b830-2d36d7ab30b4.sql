### 62. Covered Calls
**Level:** Beginner | **Timeframe:** Monthly

Sell calls against shares you own to generate income. Collect premium but cap upside at the strike price.

**Entry:** Own 100 shares. Sell 1 call 5-10% above current price, 30-45 DTE. Best in sideways or mildly bullish markets.
**Exit:** Let expire worthless or buy back for less. Roll to next month for continued income.
**Risk Management:** Downside risk remains (you still own the stock). Avoid selling calls before earnings.

