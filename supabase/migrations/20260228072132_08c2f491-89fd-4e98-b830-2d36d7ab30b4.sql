## Trend Following Strategies

"The trend is your friend"—these strategies capitalize on established market momentum.

### 5. Classic Trend Following
**Level:** Intermediate | **Timeframe:** Weeks to months

Follow established trends until a clear reversal signal appears. This is the foundation of systematic trading, used by legendary Turtle Traders.

**Entry:** Enter when price breaks above 20-day high (long) or below 20-day low (short). Confirm with ADX > 25 for trend strength.
**Exit:** Trail stop using 10-day low (for longs) or 10-day high (for shorts). Exit on opposite signal.
**Risk Management:** Position size using ATR-based volatility normalization. Risk no more than 1% per trade. Accept 40-50% win rate with large winners.
**Notable Traders:** Ed Seykota, Richard Dennis (Turtle Traders), Bill Dunn

📖 **Further Reading:** [Trend Following: Riding Market Momentum](/blog/trend-following-strategy) | [Trend Following: Aligning Patterns with the Dominant Trend](/blog/trend-following-pattern-alignment)

### 6. Breakout Trading
**Level:** Intermediate | **Timeframe:** Intraday to multi-day

Enter trades when price breaks decisively through support or resistance levels. Breakout trading captures the explosive moves that occur when supply/demand equilibrium shifts.

**Entry:** Enter on a close above resistance with volume surge (50%+ above 20-day average). Wait for a retest of the breakout level for confirmation entry. Avoid breakouts on low volume (likely false breakout).
**Exit:** Target measured move from the pattern height projected from breakout point. Trail stops below each higher low.
**Risk Management:** Place stop-loss just below the breakout level (or pattern boundary). Risk 1-2% of capital. Be prepared for false breakouts (30-40% failure rate).
**Notable Traders:** Jesse Livermore, Nicolas Darvas

📖 **Further Reading:** [Breakout Trading: Capturing Explosive Price Moves](/blog/breakout-trading-complete) | [Breakout Trading: Executing Chart Pattern Breakouts](/blog/breakout-pattern-execution)

### 7. Momentum Trading
**Level:** Intermediate | **Timeframe:** Days to weeks

Buy assets showing strong upward momentum, sell those showing downward momentum. Based on the empirical finding that recent winners tend to keep winning over 3-12 month horizons.

**Entry:** Screen for stocks with strong relative strength (top 10-20% of universe). Enter on pullbacks to rising 10 or 21-day EMA. Confirm with volume expansion and RSI above 50.
**Exit:** Exit when momentum fades—RSI divergence, moving average crossover, or relative strength ranking drops below 50th percentile.
**Risk Management:** Use trailing stops at 2x ATR. Rotate out of weakening positions monthly. Size positions based on conviction and volatility.
**Notable Traders:** Mark Minervini, William O''Neil

📖 **Further Reading:** [Momentum Trading: Following the Smart Money](/blog/momentum-trading-strategy) | [Momentum Analysis: Confirming Pattern Breakout Strength](/blog/momentum-pattern-confirmation)

### 8. Moving Average Crossover
**Level:** Beginner | **Timeframe:** Weeks to months

Use moving average crossovers to identify trend changes. When a fast MA crosses above a slow MA, it signals bullish momentum; crossing below signals bearish.

**Entry:** Buy when 50-day SMA crosses above 200-day SMA (Golden Cross). Short when 50-day crosses below 200-day (Death Cross). For faster signals, use 10/30 EMA crossover.
**Exit:** Exit when the opposite crossover occurs. Alternatively, exit when price closes below the slow MA for two consecutive days.
**Risk Management:** Lagging indicator—expect whipsaws in ranging markets. Filter signals using ADX (only take crossovers when ADX > 20). Use position sizing to manage false signals.

📖 **Further Reading:** [Moving Average Crossovers: Classic Trend Signals](/blog/moving-average-crossover-strategy) | [50/200 Moving Average Crossover: Golden Cross & Death Cross](/blog/golden-death-cross)

### 9. Donchian Channel
**Level:** Intermediate | **Timeframe:** Weeks to months

The original Turtle Trading system using price channel breakouts. Donchian Channels plot the highest high and lowest low over a lookback period, creating a visual breakout framework.

**Entry:** Buy when price breaks above 20-period high. Short when price breaks below 20-period low.
**Exit:** Exit longs when price breaks below 10-period low. Exit shorts when price breaks above 10-period high.
**Risk Management:** Risk 2% of equity per trade using ATR-based position sizing. Add to winning positions (pyramiding) at each 0.5 ATR increment.

📖 **Further Reading:** [Donchian Channel Strategy: The Turtle Trading System](/blog/donchian-channel)

### 10. Parabolic SAR
**Level:** Intermediate | **Timeframe:** Intraday to weekly

Use trailing dots above/below price to identify trend direction and potential reversals. The indicator accelerates as the trend develops, creating a natural trailing stop.

**Entry:** Enter long when dots flip from above to below price. Enter short when dots flip from below to above. Confirm direction with a trend filter (e.g., 200 EMA).
**Exit:** Exit when the SAR dots flip to the opposite side of price.
**Risk Management:** Works best in trending markets. Avoid during tight consolidation (frequent whipsaws). Combine with ADX > 25 filter.

📖 **Further Reading:** [Parabolic SAR Strategy: Trailing Stop Indicator](/blog/parabolic-sar-strategy)

### 11. ADX Trend Trading
**Level:** Intermediate | **Timeframe:** Daily

Use the Average Directional Index to measure trend strength before entering. ADX doesn''t show direction—it shows how strong the current trend is.

**Entry:** Only take trend trades when ADX > 25 (strong trend). Use +DI/-DI crossovers for direction: buy when +DI crosses above -DI with ADX rising.
**Exit:** Exit when ADX peaks and turns down (trend weakening) or when DI lines re-cross.
**Risk Management:** When ADX < 20, switch to range-bound strategies. ADX between 20-25 is a "no trade" zone.

📖 **Further Reading:** [ADX: Average Directional Index](/blog/adx-indicator)

### 12. Supertrend Strategy
**Level:** Beginner | **Timeframe:** Daily to weekly

A simplified trend-following indicator combining ATR with median price. Green line below price = uptrend; red line above = downtrend.

**Entry:** Buy when Supertrend turns green (flips below price). Short when Supertrend turns red. Default settings: period 10, multiplier 3.
**Exit:** Exit when Supertrend changes color. The indicator automatically trails as a dynamic stop-loss.
**Risk Management:** Increase ATR multiplier for fewer but more reliable signals. Combine with volume confirmation to avoid false flips.

📖 **Further Reading:** [Supertrend Strategy: Simplified Trend Following](/blog/supertrend-strategy)

