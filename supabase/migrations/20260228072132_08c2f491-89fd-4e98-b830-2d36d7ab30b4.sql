## Risk Management Strategies

### 56. Fixed Fractional Position Sizing
**Level:** Beginner | **Timeframe:** Per trade

Risk a fixed percentage of capital per trade (typically 1-2%). No single loss can significantly damage your account.

**Entry:** Position size = (Account × Risk %) / (Entry - Stop). Example: $100K, 1% risk, entry $50, stop $48 → 500 shares.
**Risk Management:** Never exceed 2%. Reduce during drawdowns (e.g., 0.5% after 10% drawdown).

📖 **Further Reading:** [Position Sizing: The Key to Long-Term Survival](/blog/position-sizing) | [Position Sizing Fundamentals](/blog/position-sizing-guide)

### 57. Kelly Criterion
**Level:** Advanced | **Timeframe:** Portfolio-level

Mathematically optimal position sizing based on your edge. Kelly = (Win Rate × Avg Win - Loss Rate × Avg Loss) / Avg Win.

**Entry:** Calculate from at least 100 trades. Most traders use Half Kelly or Quarter Kelly for safety.
**Risk Management:** Full Kelly is too aggressive. Requires accurate edge estimation—overestimating leads to overbetting and ruin.

📖 **Further Reading:** [Kelly Criterion for Position Sizing](/blog/kelly-criterion)

### 58. Anti-Martingale
**Level:** Intermediate | **Timeframe:** Per trade

Increase position size after wins, decrease after losses. Capitalizes on winning streaks while limiting damage during losing streaks.

**Entry:** After a win, increase size by 25-50%. After a loss, return to base size.
**Risk Management:** Cap maximum at 3x base. Reset after any loss. Creates convex returns (big wins on large positions, small losses on small).

📖 **Further Reading:** [Anti-Martingale Strategy: Scaling with Winners](/blog/anti-martingale) | [Martingale Strategy: Double-Down Risk Analysis](/blog/martingale-strategy)

### 59. Portfolio Hedging
**Level:** Intermediate | **Timeframe:** Ongoing

Protect portfolio during declines using puts, inverse ETFs, or VIX derivatives without liquidating core holdings.

**Entry:** Buy portfolio puts when VIX is low. Use collars (sell calls to finance puts) for zero-cost hedging.
**Exit:** Remove hedges when conditions stabilize.
**Risk Management:** Hedging costs reduce returns. Only hedge significant portfolios ($50K+). Tail-risk hedges provide catastrophic protection cheaply.

📖 **Further Reading:** [Hedging Strategies for Traders](/blog/hedging-strategies) | [Collar Strategy](/blog/collar-strategy)

### 60. Risk Parity
**Level:** Advanced | **Timeframe:** Monthly rebalancing

Allocate portfolio risk equally across asset classes, not by dollar amount. A 60/40 portfolio has 90% of its risk in stocks—risk parity equalizes this.

**Entry:** Calculate volatility per asset class. Allocate inversely proportional to volatility.
**Risk Management:** Often uses leverage. Monitor correlations—risk parity struggles when correlations spike. Founded by Ray Dalio''s All Weather.

📖 **Further Reading:** [Risk Parity Portfolio: Equal Risk Allocation](/blog/risk-parity) | [Portfolio Diversification Strategies](/blog/portfolio-diversification)

### 61. Maximum Loss Rules
**Level:** Beginner | **Timeframe:** Per session/day/week

Hard maximum loss limits that trigger mandatory trading cessation. Prevents emotional spiraling.

**Entry:** Define daily max loss (2%), weekly (5%), monthly (10%). When hit, stop trading—no exceptions.
**Risk Management:** The single most important rule for active traders. Most blown accounts result from violating max loss rules. Automate if possible.

📖 **Further Reading:** [Maximum Loss Rules for Traders](/blog/max-loss-rules) | [ATR-Based Stop Loss Strategy](/blog/atr-stop-loss) | [Dynamic Stop Loss Management](/blog/dynamic-stop-loss)

