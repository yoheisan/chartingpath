## Time-Based Trading Strategies

These strategies are defined primarily by how long you hold positions.

### 1. Buy and Hold Strategy
**Level:** Beginner | **Timeframe:** Years to decades | **Frequency:** 1–5 trades/year

A long-term investment approach where assets are purchased and held regardless of short-term fluctuations. The core thesis is that markets trend upward over time, making time in the market more valuable than timing the market.

**Entry:** Buy fundamentally strong assets at fair or undervalued prices. Focus on companies with durable competitive advantages, strong balance sheets, and consistent earnings growth.
**Exit:** Rarely exit. Only sell when fundamentals deteriorate significantly, the original investment thesis breaks, or capital is needed for retirement.
**Risk Management:** Diversify across sectors and asset classes. Rebalance annually.
**Notable Traders:** Warren Buffett, Charlie Munger, John Bogle

📖 **Further Reading:** [Buy and Hold Strategy: The Long-Term Wealth Builder](/blog/buy-and-hold-strategy)

### 2. Swing Trading
**Level:** Intermediate | **Timeframe:** Days to weeks | **Frequency:** 5–20 trades/month

A medium-term strategy that captures price "swings" within an established trend. Swing traders hold positions overnight and through multiple sessions, targeting multi-day price movements.

**Entry:** Buy at swing lows in uptrends using support levels, moving averages, or Fibonacci retracements. Short at swing highs in downtrends.
**Exit:** Target the opposite end of the swing range (resistance for longs, support for shorts). Trail stops using the previous swing low/high.
**Risk Management:** Risk 1-2% per trade. Use ATR-based stops for volatility adjustment. Avoid holding through major news events.
**Notable Traders:** Paul Tudor Jones, Mark Minervini

📖 **Further Reading:** [Swing Trading Strategy](/blog/swing-trading-strategy) | [Swing Trading Patterns: Multi-Day Pattern Confirmation](/blog/swing-trading-pattern-strategies)

### 3. Day Trading
**Level:** Advanced | **Timeframe:** Minutes to hours | **Frequency:** 5–100 trades/day

All positions are opened and closed within the same trading day. Day traders exploit intraday volatility and avoid overnight gap risk entirely.

**Entry:** Focus on setups in the first 1-2 hours of market open when volume is highest. Use Level 2, time & sales, and intraday patterns (opening range breakout, VWAP reclaim).
**Exit:** All positions closed before market close. Use time-based exits if target not hit by 3pm.
**Risk Management:** Daily max loss limit (e.g., 3% of account). Never average down on losing day trades. Use hard stops on every position.
**Notable Traders:** Ross Cameron, Andrew Aziz

📖 **Further Reading:** [Day Trading: Mastering Intraday Price Action](/blog/day-trading-strategy) | [Day Trading Patterns: Intraday Pattern Confirmation Guide](/blog/day-trading-pattern-strategies)

### 4. Scalping
**Level:** Expert | **Timeframe:** Seconds to minutes | **Frequency:** 10–200 trades/day

Ultra-short-term strategy aiming to profit from very small price movements. Scalpers exploit bid-ask spreads and micro-momentum, requiring fast execution and tight risk control.

**Entry:** Enter on minor pullbacks using 1-minute or tick charts. Look for order flow imbalances, level 2 momentum shifts, and micro-breakouts above consolidation.
**Exit:** Target 0.1%–0.5% per trade. Exit immediately if the trade doesn''t work within seconds. Never let a scalp turn into a swing trade.
**Risk Management:** Extremely tight stops (2-5 ticks). High win rate required (60%+) to overcome commission costs. Need direct market access (DMA) for execution speed.

📖 **Further Reading:** [Scalping: High-Frequency Short-Term Trading](/blog/scalping-strategy) | [Scalping with Micro-Patterns](/blog/scalping-micro-pattern-confirmation)

