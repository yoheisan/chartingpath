### 63. Protective Puts
**Level:** Beginner | **Timeframe:** Monthly to quarterly

Buy puts to insure existing stock positions against decline—portfolio insurance.

**Entry:** Buy puts 5-10% below current price, 60-90 DTE. For crash protection, buy 20-30% OTM.
**Exit:** Sell on decline (lock in hedge profit) or let expire (cost of insurance).
**Risk Management:** High cost reduces returns. Finance with covered call sales (collar strategy).

📖 **Further Reading:** [Hedging Strategies for Traders](/blog/hedging-strategies)

### 64. Iron Condor
**Level:** Intermediate | **Timeframe:** Monthly

Sell OTM put spread and call spread simultaneously. Profit from low volatility and time decay.

**Entry:** Sell when IV rank > 50%. Place short strikes at 1 standard deviation. 30-45 DTE.
**Exit:** Buy back at 50% of max profit. Close if stock threatens either short strike.
**Risk Management:** Max risk is one spread width minus credit. Keep max loss at 2-3% of portfolio. Avoid through earnings.

📖 **Further Reading:** [Iron Condor Strategy: Range-Bound Income](/blog/iron-condor)

### 65. Butterfly Spread
**Level:** Advanced | **Timeframe:** Monthly

Precision strike trading for specific price targets. Maximum profit at the middle strike at expiration.

**Entry:** Center butterfly at predicted target. 20-30 DTE. Low cost but requires precise prediction.
**Exit:** Take profit at 50%+ of max gain. Close if underlying moves well beyond wings.
**Risk Management:** Max risk is premium paid. Low probability of max profit but excellent risk/reward.

📖 **Further Reading:** [Butterfly Spread: Precision Strike Trading](/blog/butterfly-spread)

### 66. Straddle/Strangle
**Level:** Intermediate | **Timeframe:** Weekly to monthly

Profit from large moves in either direction. Straddle: buy ATM call + put. Strangle: buy OTM call + put.

**Entry:** Buy before expected volatility events. Enter when IV is low relative to expected move. 5-10 days before catalyst.
**Exit:** Sell immediately after event/vol expansion. Don''t hold through time decay.
**Risk Management:** Long straddles lose through theta daily. Only buy when expecting a move larger than cost. Calculate breakevens before entry.

📖 **Further Reading:** [Straddles and Strangles: Volatility Trading](/blog/straddle-strangle)

### 67. Delta Neutral
**Level:** Expert | **Timeframe:** Daily management

Direction-free options trading profiting from time decay, volatility changes, or gamma scalping.

**Entry:** Sell options premium and hedge delta by trading the underlying. Profit from theta while staying direction-neutral.
**Exit:** Close when options expire or profit target is met. Rebalance delta continuously.
