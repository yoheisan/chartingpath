Invest in long-term secular trends (AI, clean energy, aging populations, cybersecurity) through themed ETFs or stocks.

**Entry:** Identify themes with 10+ year growth runway. Use thematic ETFs for diversified exposure.
**Exit:** Exit when thesis is fully priced in or trend reverses.
**Risk Management:** Thematic funds can be concentrated and volatile. Many themes are overhyped at launch. Wait for pullbacks. Limit to 20-30% of portfolio.

📖 **Further Reading:** [Thematic Investing: Profiting from Megatrends](/blog/thematic-investing)

### 91. ESG Investing
**Level:** Beginner | **Timeframe:** Years

Invest based on Environmental, Social, and Governance criteria. Filter companies by sustainability, labor standards, and ethics.

**Entry:** Use ESG-screened ETFs or evaluate with MSCI/Sustainalytics ratings. Negative screening (exclude) or positive (overweight leaders).
**Exit:** Standard investment exit criteria. Sell if ESG practices deteriorate.
**Risk Management:** Can reduce diversification. "Greenwashing" risk—verify claims with third-party data.

📖 **Further Reading:** [ESG Investing: Sustainable and Responsible Returns](/blog/esg-investing)

### 92. Turnaround Trading
**Level:** Advanced | **Timeframe:** Months to years

Buy struggling companies showing recovery signs—new management, restructuring, improving fundamentals.

**Entry:** Look for catalysts: new CEO, asset sales, debt restructuring, improving quarters. Enter when price stabilizes and forms higher lows.
**Exit:** Target 50-100% gains. Exit if restructuring fails.
**Risk Management:** Many turnarounds fail. Never invest more than 2-3% per turnaround. Diversify across 5-10 candidates.

📖 **Further Reading:** [Turnaround Trading: Profiting from Corporate Recoveries](/blog/turnaround-trading)

### 93. IPO Trading
**Level:** Advanced | **Timeframe:** Days to months

Trade newly public companies. IPOs offer unique dynamics—initial volatility, lockup expirations, analyst initiation.

**Entry:** Day 1: opening range breakout. First week: pullback to IPO price. Lockup expiration: watch for selling or absorption.
**Exit:** Quick profits on day 1 momentum. Exit before lockup. Long-term: hold winners with institutional accumulation.
**Risk Management:** Extremely volatile and information-poor. Size small (0.5-1% risk). Avoid "hot" IPOs that gap 100%+.

📖 **Further Reading:** [IPO Trading: Strategies for New Public Companies](/blog/ipo-trading)

### 94. SPAC Trading
**Level:** Advanced | **Timeframe:** Weeks to months

Trade Special Purpose Acquisition Companies. SPACs have distinct phases: pre-deal (near NAV), post-rumor, post-merger.

**Entry:** Pre-deal: buy near $10 NAV for risk-free arbitrage. Post-rumor: momentum trade. Post-deal: evaluate as regular stock.
**Exit:** Sell into deal announcement pop. Redeem at NAV if deal isn''t attractive.
**Risk Management:** $10 floor before merger (downside protection). After merger, no floor—most SPACs underperform.

📖 **Further Reading:** [SPAC Trading: Special Purpose Acquisition Companies](/blog/spac-trading)

### 95. Earnings Play Trading
**Level:** Advanced | **Timeframe:** Days

Trade around quarterly earnings—the largest single-day moves for individual stocks.

**Entry:** Analyze historical reactions, options implied move, whisper numbers. Use options for defined risk. Post-earnings: gap-and-go or gap-fade setups.
**Exit:** Take profits within 1-3 days as reaction settles.
**Risk Management:** Binary events—50/50 direction. Use options. Never hold through earnings with undefined risk. Size to 0.5% max.

📖 **Further Reading:** [Earnings Season Trading: Profiting from Corporate Reports](/blog/earnings-season-trading)

### 96. Pre-Market Trading
**Level:** Advanced | **Timeframe:** 4am–9:30am ET

Trade during pre-market when liquidity is thin but price discovery occurs on overnight news.

**Entry:** Scan for high pre-market volume and significant gaps. Use limit orders only. Trade breakouts of pre-market consolidation.
**Exit:** Take profits before market open or hold if momentum is strong.
**Risk Management:** Wide spreads and low liquidity. Use limit orders exclusively. Size 50% of normal. Pre-market action can reverse at the open.

📖 **Further Reading:** [Pre-Market Trading: Early Bird Strategies](/blog/pre-market-trading)

### 97. After-Hours Trading
**Level:** Advanced | **Timeframe:** 4pm–8pm ET

Trade after the regular close. Earnings and news create immediate opportunities.

**Entry:** React to earnings releases. Use limit orders only. Focus on initial reaction direction.
**Exit:** Most liquidity dries up by 6pm. Exit or set overnight limits.
**Risk Management:** Even thinner liquidity than pre-market. Spreads 5-10x normal. Only trade mega-caps during earnings. Never use market orders.

📖 **Further Reading:** [After-Hours Trading: Post-Close Opportunities](/blog/after-hours-trading)

### 98. Penny Stock Trading
**Level:** Expert | **Timeframe:** Minutes to days

Trade micro-cap stocks (under $5) for large percentage moves. Can move 50-200% in a day but carry extreme risk.

**Entry:** Scan for unusual volume (5x+ average), news catalysts, technical breakouts. Enter on pullbacks within momentum.
**Exit:** Take profits quickly—30-50% gains should not be held. Most moves last 1-3 days.
**Risk Management:** Most penny stocks go to zero. Never invest more than 5% total. Avoid promotional campaigns ("pump and dumps"). Check SEC filings for dilution.

📖 **Further Reading:** [Penny Stock Trading: High-Risk Micro-Cap Strategies](/blog/penny-stock-trading)

### 99. Biotech Catalyst Trading
**Level:** Expert | **Timeframe:** Days to weeks

Trade around FDA decisions, clinical trials, and pipeline milestones. Binary events—stocks can double or lose 70%.

**Entry:** Research drug pipeline, approval probability, and options pricing. Use options for defined risk. Buy derisked situations.
**Exit:** Exit before binary event or hold through with options only.
**Risk Management:** Never hold concentrated stock through FDA decisions. Use options spreads. Size based on maximum defined loss.

📖 **Further Reading:** [Biotech Catalyst Trading: Binary Event Strategies](/blog/biotech-catalyst-trading)

### 100. Macro Trading
**Level:** Expert | **Timeframe:** Weeks to months

Trade based on global macroeconomic themes—interest rates, inflation, GDP, geopolitical events. The "big picture" approach used by hedge funds.

**Entry:** Identify macro regime (inflationary, deflationary, risk-on, risk-off). Position across currencies, bonds, commodities, equities. Use yield curve, PMI, CPI, unemployment.
**Exit:** Exit when macro regime shifts. Macro positions evolve—adjust as thesis develops.
**Risk Management:** Macro trades can be wrong for extended periods—use options or small sizes. Notable practitioners: George Soros, Stanley Druckenmiller, Ray Dalio.

📖 **Further Reading:** [Macro Trading: Global Economic Strategy](/blog/macro-trading)

