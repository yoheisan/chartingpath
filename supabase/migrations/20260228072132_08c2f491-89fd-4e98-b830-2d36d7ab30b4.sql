## Technical Indicator Strategies

Strategies built around specific technical indicators for precise entry/exit timing.

### 13. RSI Divergence
**Level:** Intermediate | **Timeframe:** Any

Trade divergences between RSI and price for high-probability reversal signals. When price makes a new high but RSI makes a lower high, momentum is weakening.

**Entry:** Bullish divergence: price makes lower low, RSI makes higher low—buy when RSI crosses above 30. Bearish divergence: price makes higher high, RSI makes lower high—short when RSI crosses below 70.
**Exit:** Target the midpoint of the prior move (50% retracement). Exit if divergence is invalidated by a new RSI extreme.
**Risk Management:** Wait for price confirmation (trendline break, candlestick reversal) before entering. Don''t trade divergence against strong trends (ADX > 40).

📖 **Further Reading:** [RSI Divergence Strategy: Catching Reversals](/blog/rsi-divergence-strategy) | [RSI Divergence: Validating Reversal Pattern Signals](/blog/rsi-divergence-pattern-validation)

### 14. MACD Histogram
**Level:** Intermediate | **Timeframe:** Daily to weekly

Use MACD histogram changes for momentum shift identification. The histogram measures the distance between the MACD line and signal line.

**Entry:** Buy when MACD histogram turns from negative to positive. Short when it turns from positive to negative. Strongest signals occur after histogram divergence with price.
**Exit:** Exit when histogram reverses direction or reaches extreme levels.
**Risk Management:** Filter with trend direction—only take MACD long signals when price is above 200 SMA.

📖 **Further Reading:** [MACD Strategy: Trend and Momentum Combined](/blog/macd-complete-strategy) | [MACD Divergence: Early Reversal Signals](/blog/macd-divergence-strategy) | [Using MACD to Confirm Chart Pattern Breakouts](/blog/using-macd-to-confirm-patterns)

### 15. Bollinger Band Squeeze
**Level:** Intermediate | **Timeframe:** Daily

Trade volatility expansions after Bollinger Band contractions. When bands narrow to their tightest point, a large move is imminent.

**Entry:** Identify squeeze when bandwidth drops to 6-month low. Enter in the direction of the breakout when price closes outside the bands with volume.
**Exit:** Target 2x the bandwidth at squeeze as measured move. Exit if price re-enters the bands quickly.
**Risk Management:** Place stop at the opposite band. Wait for a close outside bands, not just an intraday pierce.

📖 **Further Reading:** [Bollinger Bands Strategy: Volatility Trading](/blog/bollinger-bands-complete) | [Bollinger Bands: Confirming Pattern Volatility Breakouts](/blog/bollinger-bands-pattern-confirmation)

### 16. Stochastic Oscillator
**Level:** Intermediate | **Timeframe:** Daily to weekly

Use %K/%D crossovers in overbought/oversold zones to time entries. The stochastic measures where the current close sits relative to the high-low range.

**Entry:** Buy when %K crosses above %D below the 20 level. Short when %K crosses below %D above the 80 level.
**Exit:** Exit long when stochastic reaches 80+ zone. Exit short when it reaches 20- zone.
**Risk Management:** In strong trends, stochastic stays overbought/oversold for extended periods. Use slow stochastic (14,3,3) to reduce noise.

📖 **Further Reading:** [Stochastic Oscillator Strategy: %K/%D Crossovers](/blog/stochastic-oscillator-strategy)

### 17. Ichimoku Cloud
**Level:** Advanced | **Timeframe:** Daily to weekly

A comprehensive all-in-one system providing trend direction, support/resistance, momentum, and trade signals. The Cloud (Kumo) shows equilibrium zones.

**Entry:** Buy when price is above the cloud, Tenkan crosses above Kijun, and Chikou Span is above price 26 periods ago. Strongest signals when all 5 elements align.
**Exit:** Exit when Tenkan crosses below Kijun or price enters the cloud.
**Risk Management:** Avoid trading when price is inside the cloud (indecision zone). Thicker clouds provide stronger support/resistance.

📖 **Further Reading:** [Ichimoku Cloud Strategy: Complete Trading System](/blog/ichimoku-strategy) | [Ichimoku Cloud Complete Guide](/blog/ichimoku-cloud)

### 18. CCI (Commodity Channel Index)
**Level:** Intermediate | **Timeframe:** Daily

Identify cyclical trends using CCI extremes. CCI measures how far price has deviated from its statistical mean.

**Entry:** Buy when CCI crosses above +100 from below (new uptrend). Short when CCI crosses below -100. For mean reversion: buy below -200, sell above +200.
**Exit:** Exit long when CCI peaks and turns down from above +100.
**Risk Management:** CCI is unbound—use it with price-based stops rather than CCI-based targets.

📖 **Further Reading:** [CCI: Commodity Channel Index](/blog/cci-indicator)

### 19. Williams %R
**Level:** Intermediate | **Timeframe:** Daily

Similar to stochastic but with inverted scale (-100 to 0). Measures where the close is relative to the highest high of the lookback period.

**Entry:** Buy when %R crosses above -80 from extreme oversold territory. Short when %R crosses below -20 from extreme overbought.
**Exit:** Exit when %R reaches the opposite extreme or crosses -50 against your direction.
**Risk Management:** Very sensitive—use 14-period for swing trading, 28-period for position trading. Confirm with volume and price action.

📖 **Further Reading:** [Williams %R Indicator](/blog/williams-r)

### 20. On-Balance Volume (OBV)
**Level:** Intermediate | **Timeframe:** Daily to weekly

Use cumulative volume flow to confirm price trends and spot divergences. OBV adds volume on up days and subtracts on down days.

**Entry:** Buy when OBV makes a new high before price does (accumulation). Short when OBV makes a new low before price (distribution).
**Exit:** Exit when OBV diverges from price direction.
**Risk Management:** OBV works best on daily and weekly charts. Use as confirmation, not primary signal.

📖 **Further Reading:** [OBV: On-Balance Volume](/blog/obv-indicator) | [Volume-Based Trading: Confirming Price Moves](/blog/volume-based-trading)

### 21. VWAP Trading
**Level:** Intermediate | **Timeframe:** Intraday

Volume Weighted Average Price serves as the institutional benchmark price for the day.

**Entry:** Buy when price pulls back to VWAP from above and bounces. Short when price rallies to VWAP from below and rejects. Buy on first VWAP reclaim of the day with volume.
**Exit:** Target 2x the distance from entry to VWAP. Exit if price closes below VWAP on increasing volume.
**Risk Management:** VWAP resets each session. Most effective in the first 2 hours and last hour.

📖 **Further Reading:** [VWAP Trading Strategy: Institutional Edge](/blog/vwap-strategy) | [VWAP: Institutional Confirmation for Pattern Entries](/blog/vwap-pattern-confirmation)

### 22. Keltner Channel
**Level:** Intermediate | **Timeframe:** Daily

Uses ATR instead of standard deviation, making it less sensitive to sudden volatility spikes than Bollinger Bands.

**Entry:** Buy when price closes above the upper channel (breakout). For mean reversion: buy at lower channel, sell at upper. Combine with Bollinger squeeze for confirmation.
**Exit:** For breakout: trail stop at the middle line (EMA). For mean reversion: target the opposite channel.
**Risk Management:** Default: 20-period EMA, 2x ATR. Widen to 2.5x ATR for fewer but higher-quality signals.

📖 **Further Reading:** [Keltner Channel Strategy: ATR-Based Bands](/blog/keltner-channel-strategy)

### 23. Money Flow Index (MFI)
**Level:** Intermediate | **Timeframe:** Daily

Often called "volume-weighted RSI," MFI incorporates both price and volume to measure buying/selling pressure. More reliable than RSI alone.

**Entry:** Buy when MFI drops below 20 (oversold with volume confirmation). Short when MFI rises above 80. For divergence: buy when price makes new low but MFI makes higher low.
**Exit:** Exit when MFI reaches the opposite extreme or crosses 50 against your position.
**Risk Management:** MFI divergences are more reliable than RSI divergences because they include volume. Best on stocks and futures with reliable volume data.

📖 **Further Reading:** [Money Flow Index (MFI)](/blog/money-flow-index)

### 24. Chaikin Oscillator
**Level:** Advanced | **Timeframe:** Daily

Measures the momentum of the Accumulation/Distribution line, revealing acceleration or deceleration of money flow. A rising Chaikin Oscillator means smart money is buying.

**Entry:** Buy when the oscillator crosses above zero while price is above the 200-day MA (confirming accumulation in an uptrend). Short when it crosses below zero with price below 200 MA.
**Exit:** Exit when the oscillator reverses direction and crosses zero against your position.
**Risk Management:** Best used as secondary confirmation rather than primary signal. Less effective in low-volume environments.

📖 **Further Reading:** [Chaikin Oscillator: Volume-Based Momentum Indicator](/blog/chaikin-oscillator)

### 25. Elder Ray Index
**Level:** Advanced | **Timeframe:** Daily

Developed by Dr. Alexander Elder, this measures Bull Power and Bear Power—the distance between the high/low and a 13-period EMA. Reveals the battle between buyers and sellers.

**Entry:** Buy when Bear Power is negative but rising (bears weakening) while the 13 EMA is rising. Short when Bull Power is positive but falling while EMA is declining.
**Exit:** Exit long when Bull Power turns negative. Exit short when Bear Power turns positive.
**Risk Management:** Part of Elder''s Triple Screen system—only trade in the direction of the weekly trend.

📖 **Further Reading:** [Elder Ray Index: Bull and Bear Power Analysis](/blog/elder-ray-index)

### 26. Force Index
**Level:** Intermediate | **Timeframe:** Daily

Combines price, direction, and volume into a single oscillator. Measures the "force" behind each price move—large volume moves with big price changes have more force.

**Entry:** Buy when the 2-day Force Index dips below zero during an uptrend. Short when it rises above zero during a downtrend. Use 13-day for trend direction.
**Exit:** Exit when Force Index reaches an extreme in your favor or reverses against you.
**Risk Management:** Never trade against the 13-day Force Index direction. Best combined with Elder''s Triple Screen system.

📖 **Further Reading:** [Force Index: Price, Volume, and Direction Combined](/blog/force-index)

### 27. TRIX Indicator
**Level:** Advanced | **Timeframe:** Daily to weekly

Triple Exponential Moving Average oscillator that filters out market noise through triple smoothing. Excellent for identifying sustained trends while avoiding whipsaws.

**Entry:** Buy when TRIX crosses above its signal line or above zero. Short on opposite crossovers. TRIX divergences with price are powerful reversal signals.
**Exit:** Exit on signal line crossover against your position.
**Risk Management:** Slow indicator—best for position traders. Reduce lag by using shorter periods (12 instead of 15) at the cost of more noise.

📖 **Further Reading:** [TRIX Indicator: Triple Exponential Smoothed Momentum](/blog/trix-indicator)

### 28. Ultimate Oscillator
**Level:** Advanced | **Timeframe:** Daily

Combines momentum across three timeframes (7, 14, 28 periods) into one indicator, reducing false signals. Developed by Larry Williams.

**Entry:** Bullish signal requires: (1) bullish divergence, (2) UO low was below 30 during divergence, (3) UO breaks above the divergence high. Reverse for bearish.
**Exit:** Exit long when UO rises above 70.
**Risk Management:** Strict three-condition entry filter produces fewer but higher-quality signals. May only generate 2-4 signals per year on daily charts.

📖 **Further Reading:** [Ultimate Oscillator: Multi-Timeframe Momentum Analysis](/blog/ultimate-oscillator)

