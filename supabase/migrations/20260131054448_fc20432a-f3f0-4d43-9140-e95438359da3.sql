UPDATE learning_articles SET content = '# Diamond Top Pattern: Complete Trading Guide

The diamond top is a relatively rare reversal pattern that forms after extended uptrends. Shaped like a diamond or rhombus, this pattern represents initial broadening volatility followed by narrowing consolidation—creating a distinctive diamond shape when trendlines are drawn.

## Pattern Formation

### Structure
The diamond consists of two phases:

**Broadening Phase (Left Half):**
- Higher highs and lower lows
- Expanding volatility
- Resembles broadening formation

**Narrowing Phase (Right Half):**
- Lower highs and higher lows
- Contracting volatility
- Resembles symmetrical triangle

### Pattern Psychology
- Initial broadening shows market uncertainty
- Narrowing shows sellers gaining control
- Breakdown confirms bearish resolution

## Technical Requirements

### Trendlines
- Upper left: Rising (higher highs)
- Lower left: Falling (lower lows)
- Upper right: Falling (lower highs)
- Lower right: Rising (higher lows)

### Duration
- Typically 1-3 months on daily charts
- Shorter patterns less reliable

### Volume
- Often elevated during formation
- May spike on breakdown

## Trading the Diamond Top

### Entry
- Short on break below right lower trendline
- Volume confirmation preferred

### Stop-Loss
- Above the pattern high
- Or above the right upper trendline

### Target
- Measured move: Pattern height projected down
- Typically significant moves follow

### Success Statistics
- Success rate: 70-75%
- Often leads to extended declines
- Rare pattern worth watching for

