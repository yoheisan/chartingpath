The triple top is a reversal pattern characterized by three consecutive peaks at approximately the same price level. More rare than the double top, this pattern represents exceptionally strong resistance that buyers have failed to overcome on three separate attempts.

## Pattern Psychology

### Why Triple Tops Form

The triple top represents persistent resistance:

**First Peak:** Initial test of resistance level
**Second Peak:** Buyers try again with similar result  
**Third Peak:** Final failed attempt confirms exhaustion

Each failure strengthens the resistance zone and demoralizes buyers. By the third failure, buying enthusiasm has typically exhausted, and sellers gain control.

### Accumulation of Selling Pressure

With each peak:
- More traders recognize the resistance level
- Stop-losses accumulate above the resistance
- Short interest builds at the resistance zone
- Eventual breakdown triggers cascade selling

## Technical Requirements

### Three Peaks
- All three peaks within 3% of each other
- Clear separation between each peak (weeks apart)
- Volume typically decreases on successive peaks

### Support Level (Neckline)
- Connects the two valleys between peaks
- Break confirms the pattern
- Often becomes resistance after breakdown

### Volume Pattern
- First peak: Strong volume
- Second peak: Moderate volume
- Third peak: Weakest volume (critical)
- Breakdown: Volume expansion

## Trading the Triple Top

### Entry Strategy
- Short on close below neckline support
- Wait for retest for better risk/reward

### Stop-Loss
- Above the highest of the three peaks
- Add buffer for volatility

### Price Target
- Measured move: Pattern height projected down
- Fibonacci extensions (127.2%, 161.8%)

### Success Statistics
- Success rate: 70-75%
- Higher reliability than double top due to stronger resistance confirmation

