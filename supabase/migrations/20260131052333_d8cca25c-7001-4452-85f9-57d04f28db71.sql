In a market full of indecision and mixed signals, the marubozu stands out as a statement of absolute conviction. With no shadows to speak of, this candlestick shows that one side dominated from open to close without opposition.

### What Is a Marubozu?

The word "marubozu" comes from Japanese meaning "bald" or "shaved head"—referring to the candle''s lack of shadows:

**Bullish Marubozu**: Opens at the low, closes at the high. No shadows at either end.
**Bearish Marubozu**: Opens at the high, closes at the low. No shadows at either end.

### The Psychology of No Shadows

The absence of shadows tells a powerful story:

**Bullish Marubozu**:
- Buyers controlled from the opening bell
- No meaningful selling pressure materialized
- Bulls never gave up ground
- Closes at absolute highs—demand still exceeding supply

**Bearish Marubozu**:
- Sellers dominated from the open
- No meaningful buying support emerged
- Bears never lost control
- Closes at absolute lows—supply still exceeding demand

### Marubozu Variations

Pure marubozus are rare. More common are:

**Opening Marubozu**: No shadow at the open, small shadow at close
- Shows initial conviction but some profit-taking at end

**Closing Marubozu**: Small shadow at open, no shadow at close
- Shows increasing conviction as session progressed

Both variations carry similar implications to the full marubozu, with slightly less intensity.

### Marubozu as Continuation Signal

Marubozus often appear during strong trends as continuation patterns:

**Bullish Continuation**:
- Uptrend already in place
- Bullish marubozu confirms buyer strength
- Suggests trend will continue
- Entry on pullbacks to marubozu range

**Bearish Continuation**:
- Downtrend already in place
- Bearish marubozu confirms seller strength
- Suggests trend will continue
- Entry on rallies to marubozu range

### Marubozu as Initiation Signal

Sometimes marubozus start new trends:

**Breakout Marubozu**: Bullish marubozu breaking above resistance indicates powerful new buying
**Breakdown Marubozu**: Bearish marubozu breaking below support indicates powerful new selling

These initiation marubozus suggest the move has conviction and is likely to continue.

