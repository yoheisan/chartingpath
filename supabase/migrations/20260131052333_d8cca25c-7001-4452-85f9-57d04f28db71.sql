When three consecutive bearish soldiers march price relentlessly lower, the bulls are in full retreat. The Three Black Crows pattern signals aggressive, sustained selling that often marks the beginning of significant declines.

### Pattern Anatomy

Three Black Crows consists of three consecutive bearish candles with specific characteristics:

**Each Candle**:
- Opens within the prior candle''s body
- Closes lower than the prior candle''s close
- Has a substantial real body (not small-bodied)
- Little to no lower shadow (closes near lows)

**The Sequence**:
- Candle 1: First bearish candle after uptrend or consolidation
- Candle 2: Opens in Candle 1 body, closes lower
- Candle 3: Opens in Candle 2 body, closes even lower

### Why It Works

The Three Black Crows represents sustained selling pressure:

**No Gap Down Opens**: Each candle opens within the prior body, meaning buyers attempt to stabilize overnight but fail when trading resumes

**Progressive Lower Closes**: Each session, sellers push price to new lows

**Closing Near Lows**: Minimal lower shadows mean selling continues through session close

**Three Consecutive Days**: This isn''t a one-day wonder—it''s persistent, organized selling

### Psychological Dynamics

**Candle 1**: Initial break—could be just profit-taking, but concerning
**Candle 2**: Confirmation—buyers who bought the dip on Day 1 are underwater, starting to panic
**Candle 3**: Capitulation beginning—bears have full control, trapped bulls exiting at any price

### Quality Factors

Not all Three Black Crows patterns are equal:

**High Quality**:
- Appears after a sustained uptrend
- Forms at resistance or overbought conditions
- Each candle has minimal lower shadow
- Increasing volume through the three candles
- Third candle is the largest

**Lower Quality**:
- Appears in sideways market
- Candles have significant lower shadows
- Bodies are small or inconsistent
- Decreasing volume
- Pattern is compressed (small overall range)

