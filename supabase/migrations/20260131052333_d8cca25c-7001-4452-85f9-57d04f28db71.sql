### What Is a Kicker Pattern?

The kicker is a two-candle pattern featuring:

**Gap**: The second candle opens at or beyond the first candle''s open (not just its close)
**Opposite Direction**: The second candle moves in the opposite direction of the first
**Strong Bodies**: Both candles have substantial real bodies

### Bullish Kicker

**Formation**:
1. First candle: Bearish (red/black) with decent body
2. Gap up: Second candle opens at or above first candle''s OPEN
3. Second candle: Bullish (green/white) closing strong

**The Message**: Bears were in control, but something changed dramatically overnight. Bulls gapped price above the prior session''s starting point and never looked back.

### Bearish Kicker

**Formation**:
1. First candle: Bullish (green/white) with decent body
2. Gap down: Second candle opens at or below first candle''s OPEN
3. Second candle: Bearish (red/black) closing weak

**The Message**: Bulls were in control, but something changed dramatically overnight. Bears gapped price below the prior session''s starting point and never looked back.

### Why the Kicker Is So Powerful

The kicker is rare and powerful because of what it represents:

**Gap Past the Open**: Opening beyond the prior session''s OPEN (not just close) means overnight sentiment completely reversed
**No Overlap**: The gap means the first candle''s participants are immediately and completely underwater
**Trapped Traders**: Everyone who traded the first candle is now losing—creating fuel for continuation
**Institutional Involvement**: Gaps this significant typically require institutional activity

