**Setup**: Marubozu forms in trending market

**Entry**: On pullback to marubozu midpoint or 50% of candle
**Stop Loss**: Beyond marubozu extreme (high for bearish, low for bullish)
**Target**: Extension of the move or prior swing

**Rationale**: Traders who missed the move will buy the pullback; the marubozu zone acts as support/resistance.

#### Strategy 2: Marubozu Breakout Continuation

Trade the continuation after breakout marubozu:

**Setup**: Marubozu breaks key level

**Entry**: On break of marubozu high (bullish) or low (bearish)
**Stop Loss**: Marubozu midpoint or opposite end
**Target**: Measured move or next major level

