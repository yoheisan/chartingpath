**Marubozu After Consolidation**: Breakout signal, trade continuation
**Marubozu After Extended Move**: Possible climax, watch for exhaustion
**Marubozu at Support/Resistance**: Strong signal of level break/hold
**Marubozu Mid-Trend**: Trend acceleration, look for pullback entries

### Common Marubozu Mistakes

1. **Chasing the Marubozu**: Entering at the close is often chasing—wait for pullback
2. **Ignoring Context**: A marubozu in a counter-trend move may just be a correction
3. **Not Allowing for Variations**: Near-marubozus (tiny shadows) carry similar weight
4. **Expecting Immediate Continuation**: Even strong signals need time to develop

### Marubozu on Different Timeframes

**Daily Marubozu**: Strong trend signal, reliable for swing trades
**4-Hour Marubozu**: Good for shorter-term swing entries
**1-Hour/Lower**: Useful for timing entries, but less reliable for direction

Higher timeframe marubozus override lower timeframe signals.

