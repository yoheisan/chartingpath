## Fundamental Analysis for Commodity Markets

### Supply-Side Factors
- **Production data**: OPEC+ quotas, mining output reports, crop harvest estimates
- **Inventory levels**: EIA weekly petroleum reports, LME metals warehouse stocks, USDA crop stocks
- **Geopolitical risk**: Middle East tensions affect oil; sanctions affect metals; trade disputes affect agriculture
- **Weather patterns**: La Nina/El Nino cycles dramatically impact agricultural and energy commodities

### Demand-Side Factors
- **Global GDP growth**: Industrial commodities move closely with global economic activity
- **China demand**: China is the world's largest consumer of most industrial commodities
- **Seasonal patterns**: Heating oil demand peaks in winter; gasoline peaks in summer; agricultural demand follows crop cycles

