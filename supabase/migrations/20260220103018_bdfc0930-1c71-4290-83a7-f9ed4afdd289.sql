## Risk Management for Commodity Traders

1. **Size conservatively**: Never risk more than 1-2% of capital on a single commodity position
2. **Respect leverage**: Futures leverage can amplify losses beyond your initial margin
3. **Use stops**: Volatility-adjusted stops (ATR-based) are preferred in commodity markets
4. **Diversify across commodities**: A portfolio spanning energy, metals, and agriculture reduces concentration risk
5. **Monitor roll costs**: For ETF holders, contango can erode returns significantly over time

