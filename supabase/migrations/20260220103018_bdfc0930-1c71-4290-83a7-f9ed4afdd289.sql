## Key Commodity Market Trends

### Energy Transition and Oil Volatility
The ongoing shift toward renewable energy is reshaping energy commodity markets. Natural gas is emerging as a transition fuel, seeing increased demand even as coal usage declines.

### The Commodity Supercycle
Some analysts argue we are in a new commodity supercycle driven by:
- Underinvestment in mining and energy infrastructure over the past decade
- Massive demand from green energy transition (copper for EVs and solar, lithium for batteries)
- Deglobalisation and supply chain reshoring increasing raw material demand

### Agricultural Demand Growth
Rising middle-class populations in Asia and Africa are driving structural increases in food commodity demand. Climate change is simultaneously creating supply disruptions, creating sustained upward pressure on agricultural prices.

### US Dollar Correlation
Commodities are predominantly priced in US dollars. A weaker dollar makes commodities cheaper for foreign buyers, boosting demand and prices. Understanding dollar cycles is essential for commodity traders.

