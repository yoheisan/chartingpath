## Commodity Trading Strategies

### Trend Following Strategy
Enter long positions in commodities making new multi-week highs with expanding volume. Use a trailing stop based on ATR (e.g., 2x ATR below the highest close).

### Seasonal Trade Strategy
Buy crude oil in late January-February ahead of the summer driving season rally. Sell/short natural gas in early November after winter positioning is complete.

### Spread Trading
Trade the price difference between related commodities:
- **Crack Spread**: Crude oil vs. refined products (gasoline, heating oil)
- **Crush Spread**: Soybeans vs. soybean meal and soybean oil
- **Precious Metals Spread**: Gold/Silver ratio

### News Trading
Major commodity-specific reports create tradeable volatility:
- **EIA Petroleum Status Report** (Wednesday): Crude oil and products inventories
- **USDA WASDE Report** (monthly): World Agricultural Supply and Demand Estimates
- **OPEC Monthly Oil Market Report**: Production quotas and demand forecasts

