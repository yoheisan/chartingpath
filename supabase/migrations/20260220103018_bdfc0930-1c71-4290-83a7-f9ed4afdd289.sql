## How Commodity Trading Works

### Futures Contracts
The primary vehicle for commodity trading is the **futures contract** — a standardised agreement to buy or sell a specific quantity of a commodity at a predetermined price on a future date.

Key futures concepts:
- **Contract specifications**: Each commodity has defined lot sizes (e.g., crude oil = 1,000 barrels per contract)
- **Expiration and rollover**: Futures expire monthly or quarterly; traders must roll positions to avoid physical delivery
- **Leverage**: Futures require margin deposits, providing significant leverage
- **Backwardation vs. Contango**: Market structure affects roll yield for longer-term positions

### Alternative Ways to Trade Commodities
- **ETFs and ETNs**: Commodity ETFs like GLD (gold) or USO (oil) provide equity-market-style access
- **Commodity Stocks**: Mining companies, oil producers, and agricultural firms offer leveraged exposure
- **CFDs**: Contracts for Difference allow retail traders to speculate without futures accounts
- **Options**: Commodity options provide defined-risk exposure

