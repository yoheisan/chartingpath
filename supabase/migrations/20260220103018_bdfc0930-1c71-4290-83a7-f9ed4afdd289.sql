## The Four Major Commodity Categories

### 1. Energy
Energy commodities are the highest-volume traded in the world:
- **Crude Oil (WTI and Brent)**: The world's most traded commodity. Price is influenced by OPEC+ production decisions, US inventory data, and global demand cycles.
- **Natural Gas**: Highly seasonal, driven by weather patterns, storage levels, and LNG export capacity.
- **Heating Oil and Gasoline**: Refined products with their own supply/demand dynamics tied to refinery capacity.

### 2. Metals
Metals divide into precious and industrial subcategories:
- **Precious Metals**: Gold, silver, platinum, palladium. Gold is the classic safe-haven asset and inflation hedge.
- **Industrial/Base Metals**: Copper, aluminium, zinc, nickel. Copper is often called "Dr. Copper" for its ability to signal global economic health.

### 3. Agriculture
Agricultural commodities are driven by weather, crop cycles, geopolitical events, and currency movements:
- **Grains**: Wheat, corn, soybeans — the backbone of global food supply
- **Softs**: Coffee, cocoa, sugar, cotton, orange juice
- **Oilseeds**: Soybean oil, canola — increasingly relevant in biofuel markets

### 4. Livestock
Livestock markets trade live cattle, feeder cattle, and lean hogs. These markets are influenced by feed costs, disease outbreaks, seasonal slaughter patterns, and consumer demand shifts.

