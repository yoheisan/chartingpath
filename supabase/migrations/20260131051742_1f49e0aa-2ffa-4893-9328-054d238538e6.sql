The S&P 500 can rally while most stocks fall—a few mega-cap names mask the weakness beneath. Market breadth indicators reveal this hidden divergence, showing you whether rallies are broad-based and sustainable or narrow and vulnerable.

### What Is Market Breadth?

Market breadth measures the degree of participation in a market move. Strong breadth means many stocks are participating; weak breadth means few stocks are driving the index.

**Why It Matters**: A rally led by 10 stocks is fragile. When those leaders stumble, the index has no support. A rally with 400+ stocks participating is robust—leadership can rotate without breaking the trend.

### Key Breadth Indicators

#### Advance-Decline Line (A/D Line)

The cumulative total of advancing stocks minus declining stocks:

**Calculation**: A/D Line = Previous A/D + (Advancing Issues - Declining Issues)

**Interpretation**:
- A/D Line rising with market: Confirmation of healthy trend
- A/D Line falling while market rises: Warning of internal weakness
- A/D Line divergence often leads price by weeks or months

**Historical Example**: In 2007, the S&P 500 made new highs in October, but the A/D Line peaked in June—a four-month warning before the crash.

#### New Highs vs. New Lows

Counts stocks making new 52-week highs versus new 52-week lows:

**Calculation**: Net New Highs = New 52-Week Highs - New 52-Week Lows

**Interpretation**:
- Bull markets show expanding new highs
- Bear markets show expanding new lows
- Divergence between new highs and index highs signals weakness

**Threshold Levels**:
- Net new highs > 100: Bullish confirmation
- Net new lows > 100: Bearish confirmation
- Crossover from positive to negative (or vice versa): Potential trend change

#### McClellan Oscillator

A momentum indicator of the A/D Line:

**Calculation**: 19-day EMA of A/D Difference - 39-day EMA of A/D Difference

**Interpretation**:
- Above zero: Short-term breadth is positive
- Below zero: Short-term breadth is negative
- Extreme readings (±100) suggest overbought/oversold conditions

**Trading Application**: Buy when oscillator crosses above zero from oversold; sell when it crosses below from overbought.

#### McClellan Summation Index

Cumulative total of the McClellan Oscillator:

**Interpretation**:
- Rising above zero: Intermediate-term breadth bullish
- Falling below zero: Intermediate-term breadth bearish
- Key levels: +500 (overbought), -500 (oversold), crossover of zero

#### Percentage of Stocks Above Moving Averages

Shows what percentage of stocks are in uptrends:

**Common Measures**:
- % above 200 SMA: Long-term trend health
- % above 50 SMA: Intermediate-term trend health
- % above 20 SMA: Short-term trend health

**Interpretation**:
- > 70% above 200 SMA: Strong bull market
- < 30% above 200 SMA: Strong bear market or oversold
- Divergence between % above and index price: Warning signal

