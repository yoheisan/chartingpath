No market exists in isolation. Stocks, bonds, commodities, and currencies form an interconnected web where movements in one asset class ripple through others. Understanding these relationships gives you an edge that most retail traders completely miss.

### The Four Asset Classes

Intermarket analysis focuses on relationships between:

**Stocks (Equities)**: Ownership in companies, representing economic growth expectations

**Bonds (Fixed Income)**: Debt instruments reflecting interest rate expectations and risk appetite

**Commodities**: Physical goods including energy, metals, and agriculture

**Currencies**: Relative value between national monetary units

### The Classic Intermarket Relationships

John Murphy, the pioneer of intermarket analysis, documented these classical relationships:

**Bonds and Stocks**: In a normal economy, bonds and stocks often move together during risk-on periods. However, in crisis periods, bonds rally as stocks fall (flight to safety).

**Dollar and Commodities**: Most commodities are priced in dollars. When the dollar strengthens, commodity prices typically fall, and vice versa. This is an inverse relationship.

**Commodities and Bonds**: Rising commodity prices suggest inflation, which is negative for bonds. Thus, commodities and bonds often move inversely.

**Commodities and Stocks**: Resource-heavy economies and commodity-related sectors move with commodity prices. Rising oil can be bullish for energy stocks but potentially bearish for airlines.

### The Inflation/Deflation Framework

The business cycle drives intermarket relationships through inflation dynamics:

**Inflationary Environment**:
- Commodities outperform
- Bonds underperform (yields rise)
- Commodity-related stocks outperform
- Dollar may weaken initially
- Central banks tighten policy

**Deflationary Environment**:
- Bonds outperform (yields fall)
- Commodities underperform
- Growth stocks may outperform value
- Dollar may strengthen (safe haven)
- Central banks ease policy

