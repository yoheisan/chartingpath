Rare but powerful signals:

**Zweig Breadth Thrust**: When the 10-day moving average of advancing issues / total issues moves from below 0.40 to above 0.615 within 10 days, it signals the beginning of a new bull market. These signals are rare but highly reliable.

**Whaley Breadth Thrust**: Three-day RSI of advancing issues exceeds 90. Signals powerful momentum that historically leads to further gains.

### Sector Breadth Analysis

Apply breadth concepts to sectors:

**Sector Rotation Health**: When multiple sectors show strong breadth, the rally is broad-based. When only one or two sectors carry the market, risk increases.

**Leadership Assessment**: Which sectors have the best breadth? Those are likely to continue leading.

### Divergence Trading with Breadth

The most valuable breadth signal is divergence:

**Bearish Divergence Setup**:
1. Index makes new high
2. A/D Line fails to confirm (makes lower high)
3. Net new highs declining
4. Fewer stocks above key moving averages

**Action**: Reduce exposure, tighten stops, avoid new longs in weaker stocks

**Bullish Divergence Setup**:
1. Index makes new low
2. A/D Line makes higher low
3. Net new lows declining
4. More stocks crossing above moving averages

**Action**: Prepare for potential bottom, identify leaders for when trend turns

### Practical Breadth Analysis Workflow

**Daily Check**:
1. A/D Line direction vs. index direction
2. Net new highs vs. new lows
3. % of stocks above 50-day and 200-day MA

**Weekly Assessment**:
1. McClellan Summation Index level and direction
2. Sector breadth comparison
3. Look for divergences developing

**Trading Decisions**:
- Strong breadth: Add to positions, hold winners
- Weakening breadth: Tighten stops, reduce size
- Divergence: Prepare for potential reversal

