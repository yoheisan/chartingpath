Flags and pennants are among the most reliable and frequently occurring continuation patterns in technical analysis. This comprehensive guide covers both pattern families, providing professional frameworks for identification, trading, and risk management.

## Understanding Continuation Patterns

### What Are Flags and Pennants?

Both patterns share a common structure:

**Components:**
1. **Flagpole**: Strong directional move establishing the trend
2. **Consolidation**: Brief pause in a flag or pennant shape
3. **Breakout**: Continuation in the direction of the flagpole

### Flag vs Pennant Distinction

**Flags:**
- Parallel or slightly angled channel
- Rectangle-like consolidation
- Typically longer duration

**Pennants:**
- Converging symmetrical trendlines
- Triangle-like consolidation
- Typically shorter duration

### Bullish vs Bearish

**Bullish (Uptrend Continuation):**
- Flagpole moves up
- Flag/pennant drifts down or sideways
- Breakout is upward

**Bearish (Downtrend Continuation):**
- Flagpole moves down
- Flag/pennant drifts up or sideways
- Breakout is downward

## The Flagpole: Foundation of the Pattern

### Ideal Flagpole Characteristics

**Price Movement:**
- Strong, relatively straight directional move
- Significant magnitude (5-25% typical)
- Near-vertical price action
- Clearly distinguished from prior action

**Volume Requirements:**
- Above average volume
- Expanding volume ideal
- Shows conviction behind the move

**Duration:**
- Days to weeks
- Should be impulsive, not grinding
- Clear momentum visible

### Flagpole Quality Assessment

**High Quality:**
- Single strong thrust
- Minimal overlapping candles
- High volume throughout
- Clear directional clarity

**Lower Quality:**
- Multiple pauses during move
- Choppy price action
- Inconsistent volume
- Less reliable patterns

## The Consolidation Phase

### Flag Consolidation

**Shape:**
- Parallel channel or slight angle
- Drifts against prior trend
- Rectangle appearance

**Duration:**
- 1-4 weeks typical
- Should be proportionally shorter than flagpole
- Maximum: 8 weeks

**Depth:**
- Should not exceed 50% of flagpole
- 25-38% retracement ideal
- Deeper = weaker pattern

### Pennant Consolidation

**Shape:**
- Symmetrical triangle
- Converging trendlines
- No horizontal line

**Duration:**
- 1-3 weeks typical
- Shorter than flags
- Very brief = stronger pattern

**Depth:**
- Should not exceed 50% of flagpole
- Often tighter than flags

### Volume During Consolidation

**Critical Requirement:**
- Volume should decline during consolidation
- This shows lack of conviction against the trend
- Increasing volume during consolidation = warning sign

## Trading Strategies

### Entry Methods

**Method 1: Breakout Entry**
- Wait for close beyond consolidation boundary
- Enter on confirmation candle
- Most conservative approach

**Method 2: Retest Entry**
- Wait for breakout then pullback
- Enter on successful retest
- Better risk/reward

**Method 3: Anticipatory Entry**
- Enter within consolidation near support/resistance
- Very tight stop required
- Highest risk/reward

### Stop-Loss Strategies

**For Bullish Patterns:**
- Below consolidation low
- Or below breakout candle low
- Pattern invalid below 50% flagpole retracement

**For Bearish Patterns:**
- Above consolidation high
- Or above breakdown candle high
- Pattern invalid above 50% flagpole retracement

### Price Targets

**Measured Move (Primary Target):**
- Measure flagpole length
- Add/subtract from breakout point
- Conservative: 70-80% of measured move
- Aggressive: 100-127% of measured move

**Example (Bullish Flag):**
- Flagpole: $40 to $55 = $15
- Breakout at $53
- Target: $53 + $15 = $68

### Trade Management

**After Entry:**
1. Set stop at invalidation level
2. Move to breakeven at 1:1 profit
3. Take 50% at measured move target
4. Trail remainder with trend

## Pattern Reliability Factors

### High Probability Setups

- Clear, strong flagpole
- Volume decline during consolidation
- Tight consolidation (shallow retracement)
- Short duration consolidation
- Volume expansion on breakout
- Trend-aligned with higher timeframe

### Lower Probability Setups

- Weak or choppy flagpole
- Volume increase during consolidation
- Deep retracement (>50%)
- Extended consolidation (>8 weeks)
- Low volume breakout
- Counter to higher timeframe trend

