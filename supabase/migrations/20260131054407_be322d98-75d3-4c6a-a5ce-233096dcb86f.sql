UPDATE learning_articles SET content = '# Double Top and Double Bottom Patterns: Complete Trading Guide

Double top and double bottom patterns are classic reversal formations that occur when price tests a level twice and fails to break through. These M-shaped (double top) and W-shaped (double bottom) patterns are among the most recognizable and tradeable chart formations.

## Understanding Double Top Patterns

### Pattern Psychology

The double top represents failed attempts to reach higher prices:

**First Peak:**
- Price reaches new high territory
- Profit-taking causes pullback
- Sets the resistance level

**Valley:**
- Pullback finds support
- Creates the "neckline" level
- Buyers regroup for another attempt

**Second Peak:**
- Price returns to test first peak level
- Fails to break through
- Sellers defend resistance successfully

**Breakdown:**
- Price falls below valley support
- Confirms the reversal pattern
- Triggers selling and stop-losses

### Technical Requirements

**Two Peaks:**
- Should reach approximately same price level (within 3%)
- Second peak can be slightly higher or lower
- Clear separation between peaks

**Valley (Neckline):**
- Clear support level between peaks
- Break of this level confirms pattern

**Volume Pattern:**
- First peak: Strong volume
- Second peak: Typically lower volume
- Breakdown: Volume expansion

### Trading the Double Top

**Entry:**
- Short on close below valley support
- Or wait for retest of broken support

**Stop-Loss:**
- Above the higher of the two peaks
- Add buffer for volatility

**Target:**
- Measured move: Pattern height (peak to valley) projected down
- Prior support levels

## Understanding Double Bottom Patterns

### Pattern Psychology

The double bottom represents failed attempts to reach lower prices:

**First Trough:**
- Price reaches new low territory
- Buying interest causes bounce
- Sets the support level

**Peak:**
- Bounce finds resistance
- Creates the "neckline" level
- Sellers regroup for another attempt

**Second Trough:**
- Price returns to test first trough level
- Fails to break through
- Buyers defend support successfully

**Breakout:**
- Price rises above peak resistance
- Confirms the reversal pattern
- Triggers buying and short covering

### Technical Requirements

**Two Troughs:**
- Should reach approximately same price level (within 3%)
- Second trough can be slightly higher or lower
- Clear separation between troughs

**Peak (Neckline):**
- Clear resistance level between troughs
- Break of this level confirms pattern

**Volume Pattern:**
- First trough: May see capitulation volume
- Second trough: Often lower volume
- Breakout: Volume expansion

### Trading the Double Bottom

**Entry:**
- Long on close above peak resistance
- Or wait for retest of broken resistance

**Stop-Loss:**
- Below the lower of the two troughs
- Add buffer for volatility

**Target:**
