The head and shoulders is widely considered the most reliable reversal pattern in technical analysis. This classic formation marks the end of uptrends with exceptional accuracy when properly identified and traded. Its distinctive three-peak structure provides clear entry, stop, and target levels.

## Pattern Psychology

### Why Head and Shoulders Forms

The pattern represents the exhaustion of buying momentum:

**Left Shoulder:**
- Final push higher in uptrend
- Strong volume on rally
- Pullback tests buyer commitment

**Head:**
- Last attempt to reach new highs
- Often on declining volume (warning sign)
- Creates the highest point of the pattern

**Right Shoulder:**
- Failed rally attempt
- Unable to reach head''s height
- Volume typically much lower than head
- Shows clear buyer exhaustion

**Neckline Break:**
- Final confirmation of reversal
- Often sees volume expansion
- Triggers stops and new short positions

### The Distribution Process

Head and shoulders typically forms during institutional distribution:

1. **Accumulation Ends**: Smart money stops buying
2. **Distribution Begins**: Large holders sell into rallies
3. **Lower High Forms**: Right shoulder shows declining demand
4. **Support Breaks**: Neckline break confirms reversal

## Technical Anatomy

### Required Elements

**Three Peaks:**
- Left Shoulder: Lower than head
- Head: Highest point in pattern
- Right Shoulder: Lower than head, often similar height to left shoulder

**Neckline:**
- Connects the lows between peaks
- Can be horizontal, ascending, or descending
- Critical level for confirmation

**Symmetry (Preferred but not required):**
- Balanced shoulders add reliability
- Duration symmetry strengthens pattern
- Asymmetry doesn''t invalidate pattern

### Pattern Measurements

**Pattern Height:**
- Vertical distance from head to neckline
- Used for price target calculation

**Pattern Duration:**
- Weeks to months on daily charts
- Proportional to magnitude of prior uptrend

## Trading the Head and Shoulders

### Entry Strategies

**Conservative Entry (Recommended):**
- Wait for daily close below neckline
- Enter on close or next bar open
- Volume confirmation preferred

**Aggressive Entry:**
- Enter on right shoulder formation
- Short when price fails at shoulder resistance
- Requires clear pattern recognition
- Higher risk if pattern doesn''t complete

**Retest Entry:**
- Wait for neckline break and pullback
- Enter when neckline acts as resistance
- Often provides best risk/reward
- May miss fast breakdowns

### Stop-Loss Placement

**Conservative Stop:**
- Above the right shoulder high
- Accounts for false breakouts
- Wider stop = smaller position size

**Aggressive Stop:**
- Above the neckline after breakdown
- Tighter risk but vulnerable to retests

### Price Target Calculation

**Measured Move Method:**
1. Calculate distance from head to neckline
2. Project this distance below the neckline

**Example:**
- Head at $60
- Neckline at $50
- Pattern height = $10
- Target = $50 - $10 = $40

**Extended Targets:**
- 127.2% of pattern height
- 161.8% Fibonacci extension
- Prior significant support levels

## Pattern Variations

### Head and Shoulders with Rising Neckline

- More common than horizontal neckline
- Often seen in strong uptrends
- May indicate less severe reversal
- Break still valid but may be less dramatic

### Head and Shoulders with Falling Neckline

- Less common
- Strongly bearish implication
- Often leads to accelerated decline
- Earlier break confirmation

### Complex Head and Shoulders

- Multiple left or right shoulders
- Double or triple head formation
- Takes longer to form
- Same trading principles apply

