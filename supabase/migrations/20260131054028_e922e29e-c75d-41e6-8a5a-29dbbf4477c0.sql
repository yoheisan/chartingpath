Breakouts typically occur in the "optimal breakout zone":

- **Too Early** (First Third): Pattern may not be complete
- **Optimal** (Middle Third): Highest probability zone
- **Too Late** (Final Third/Apex): Reduced reliability

**The Two-Thirds Rule:**
Most reliable breakouts occur approximately 2/3 of the distance from the pattern''s start to the apex.

### Volume at Breakout

**Confirming Volume:**
- 50-100%+ above 20-day average
- Shows institutional participation
- Validates the directional move

**Suspicious Low Volume:**
- Often leads to false breakouts
- Wait for retest and confirmation
