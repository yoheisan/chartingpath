Triangle patterns are among the most reliable chart formations in technical analysis. This guide covers all three major types—ascending, descending, and symmetrical—providing traders with a complete framework for identification, trading, and risk management.

## Understanding Triangle Patterns

### What Are Triangle Patterns?

Triangles are consolidation patterns formed when price action creates converging trendlines. They represent periods of decreasing volatility where buyers and sellers reach temporary equilibrium before a decisive breakout.

**Key Characteristics:**
- Formed by two converging trendlines
- Price range compresses over time
- Volume typically decreases during formation
- Breakout occurs with volume expansion

### The Three Triangle Types

**1. Ascending Triangle (Bullish Bias)**
- Flat horizontal resistance
- Rising support trendline
- Breakout typically upward (70%)

**2. Descending Triangle (Bearish Bias)**
- Flat horizontal support
- Falling resistance trendline
- Breakdown typically downward (64%)

**3. Symmetrical Triangle (Neutral)**
- Both trendlines converge equally
- No inherent directional bias
- Direction determined by prior trend

## Pattern Formation Psychology

### Ascending Triangle Psychology

The ascending triangle shows buyers becoming more aggressive:

- **Higher Lows**: Buyers unwilling to wait for lower prices
- **Flat Resistance**: Sellers defending a level
- **Resolution**: Eventually seller supply exhausts

This pattern often precedes significant breakouts because it shows accumulation—informed buyers accumulating positions at progressively higher prices.

### Descending Triangle Psychology

The descending triangle shows sellers becoming more aggressive:

- **Lower Highs**: Sellers unwilling to wait for higher prices
- **Flat Support**: Buyers defending a level
- **Resolution**: Eventually buyer demand exhausts

This pattern often precedes significant breakdowns because it shows distribution—informed sellers distributing positions at progressively lower prices.

### Symmetrical Triangle Psychology

The symmetrical triangle shows equilibrium:

- **Lower Highs**: Some selling pressure
- **Higher Lows**: Some buying pressure
- **Resolution**: One side eventually overpowers the other

The prior trend typically determines the breakout direction, making context essential.

