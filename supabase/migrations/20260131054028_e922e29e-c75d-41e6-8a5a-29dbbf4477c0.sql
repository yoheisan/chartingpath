**Entry:**
- Enter on close outside trendline
- Confirm with volume expansion
- Consider waiting for next bar confirmation

**Stop-Loss:**
- Ascending: Below rising trendline or last higher low
- Descending: Above falling trendline or last lower high
- Symmetrical: Beyond opposite trendline

**Target:**
- Measured move: Pattern height added/subtracted from breakout

### Pullback Trading

**Entry:**
- Wait for breakout, then pullback to broken trendline
- Enter when pullback finds support/resistance
- Often provides better risk/reward

**Advantages:**
- Confirms breakout validity
- Tighter stop-loss possible
- Better entry price

### Anticipatory Trading (Advanced)

**Entry:**
- Take position based on pattern bias and context
- Enter on bounce from trendline
- Very tight stop required

**Risk:**
- Highest risk approach
- Pattern may break opposite direction
- Only for experienced traders

