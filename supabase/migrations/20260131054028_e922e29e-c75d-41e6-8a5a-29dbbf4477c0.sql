## Common Mistakes to Avoid

1. **Entering Before Breakout**: Wait for confirmation
2. **Ignoring Volume**: Low volume breakouts often fail
3. **Wrong Pattern Identification**: Ensure resistance is truly flat
4. **Oversizing Positions**: Use proper position sizing
5. **Ignoring Overall Trend**: Pattern works best in uptrends
6. **Setting Targets Too Low**: Trust the measured move

## Best Context for Trading

- Within established uptrend (continuation)
- After pullback in strong trend
- With supportive market conditions
- When sector/industry is strong
- With volume declining during formation

