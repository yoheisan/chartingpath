**Bump Height:**
- Measure maximum deviation from lead-in trendline
- Significant distance indicates overextension

**Bump Duration:**
- Typically 1-3 months
- Should be proportional to lead-in

**Trendline Angle:**
- Lead-in: 30-45 degrees
- Bump: 60-90 degrees (steeper)
- Clear angle difference required

### Confirmation

- Price must return to and break the lead-in trendline
