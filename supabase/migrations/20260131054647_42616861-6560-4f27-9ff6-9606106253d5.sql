**Phase 1: Lead-In**
- Gentle uptrend or downtrend
- Moderate slope (typically 30-45 degrees)
- Establishes the base trendline
- Duration: Often 1+ months

**Phase 2: Bump**
- Speculative excess phase
- Steep acceleration (60+ degree slope)
- Price separates dramatically from trendline
- Volume often increases
- Duration: Typically shorter than lead-in

**Phase 3: Run (Reversal)**
- Price returns to trendline
- Breaks through trendline
- Accelerated move in opposite direction
- Confirms pattern completion

