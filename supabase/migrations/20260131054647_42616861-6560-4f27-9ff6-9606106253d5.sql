The broadening formation, also known as a megaphone pattern, is characterized by price action that expands over time—creating higher highs AND lower lows. This pattern represents increasing volatility and market uncertainty, often appearing at major market turning points.

## Pattern Psychology

### Why Broadening Formations Form

The broadening formation reflects growing market instability:

**Emotional Extremes:**
- Bulls and bears both become more aggressive
- Each side pushes price to new extremes
- Volatility expands as conviction increases
- Market participants become increasingly emotional

**Institutional Activity:**
- Smart money may be distributing or accumulating
- Large orders create sharp reversals
- Liquidity becomes more challenging
- Price swings widen as large players act

### Market Phase Significance

Broadening formations often appear:
- At major market tops (most common)
- During transitional phases
- Ahead of significant breakouts
- During news-driven volatility

## Technical Anatomy

### Required Elements

**Diverging Trendlines:**
- Upper trendline: Connects progressively higher highs
- Lower trendline: Connects progressively lower lows
- Lines diverge (opposite of triangle convergence)

**Swing Sequence:**
- Minimum 2 higher highs on upper trendline
- Minimum 2 lower lows on lower trendline
- Total of 5 reversal points typical

### Pattern Duration
- Typically weeks to months on daily charts
- Intraday patterns less reliable
- Longer patterns = more significant

### Volume Characteristics
- Volume often increases with volatility
- Spike volume at reversal points
- May show climactic volume at pattern completion

## Trading the Broadening Formation

### Strategy 1: Trade the Swings

**Within Pattern:**
- Long at lower trendline, target upper trendline
- Short at upper trendline, target lower trendline
- Use tight stops as reversals are sharp

**Risk Management:**
- Small position sizes due to volatility
- Quick profit-taking required
- High failure rate if pattern breaks

### Strategy 2: Trade the Breakout

**Breakout Entry:**
- Wait for decisive close beyond trendlines
- Volume confirmation important
- Often produces significant moves

**Stop-Loss:**
- Inside the pattern
- Below/above the most recent swing

**Target:**
- Measured move: Pattern height at widest point
- Often leads to extended trends

## Pattern Variations

### Broadening Top (Bearish)
