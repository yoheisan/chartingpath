### Step-by-Step Process

1. **Identify Point A**: Start of impulse move
2. **Identify Point B**: End of impulse move
3. **Measure AB**: Calculate move size
4. **Identify Point C**: End of retracement
5. **Project D**: Add/subtract AB from C
6. **Trade or Target**: Enter at C or use D as target

### Risk Management

**Entry at C:**
- Stop below C for longs
- Stop above C for shorts
- Target measured move D

**Position Sizing:**
- Risk 1-2% of account
- Calculate based on C-stop distance

