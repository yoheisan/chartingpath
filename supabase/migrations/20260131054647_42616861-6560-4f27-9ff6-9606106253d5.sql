The measured move pattern, also known as the AB=CD pattern, is a price projection technique where the second leg of a move equals the first leg in magnitude. This pattern provides clear price targets based on symmetry in market moves.

## Pattern Structure

### Basic Concept

**The AB=CD Structure:**
- A to B: First directional move (impulse)
- B to C: Retracement (correction)
- C to D: Second directional move (equals A to B)
- Point D is the target

### Pattern Psychology

**Why Measured Moves Work:**
- Markets tend toward symmetry
- Similar forces drive each leg
- Profit-taking at measured targets
- Self-fulfilling as traders use same targets

## Technical Anatomy

### Bullish Measured Move (Up)

1. **Leg AB**: Initial rally from A to B
2. **Retracement BC**: Pullback from B to C
3. **Leg CD**: Second rally, where CD = AB in points

**Calculation:**
- Target D = C + (B - A)

**Example:**
- A = $50, B = $60 (leg = $10)
- C = $55 (retracement)
- D = $55 + $10 = $65

### Bearish Measured Move (Down)

1. **Leg AB**: Initial decline from A to B
2. **Retracement BC**: Rally from B to C
3. **Leg CD**: Second decline, where CD = AB in points

**Calculation:**
- Target D = C - (A - B)

**Example:**
- A = $60, B = $50 (leg = $10)
- C = $55 (retracement)
- D = $55 - $10 = $45

## Trading the Measured Move

### As Price Target

**Using for Exit:**
- Calculate measured move target
- Take profits at or before point D
- Often sees resistance/support at D

**Partial Exits:**
- 50% at 1:1 ratio
- 75% at 0.618 of AB
- 100% at measured move

### As Entry Signal

**Trading the CD Leg:**
- Enter at point C confirmation
- Target point D
- Stop below/above point C

**Entry Confirmation:**
- Wait for reversal signal at C
- Volume confirmation helpful
- Higher timeframe alignment

## Pattern Variations

### Extended Measured Move

**CD Greater Than AB:**
- Strong momentum may extend
