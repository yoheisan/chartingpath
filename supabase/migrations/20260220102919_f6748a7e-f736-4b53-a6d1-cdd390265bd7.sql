  $content$## What Is the Three Rising Valleys Pattern?

The Three Rising Valleys is a bullish reversal chart pattern that forms after a downtrend. It consists of three successive troughs (valleys), with each valley forming at a higher price level than the previous one. This sequence of higher lows signals that selling pressure is gradually diminishing and buying interest is increasing.

---

## How to Identify Three Rising Valleys

### The Three Core Elements

**1. Prior Downtrend**
The pattern must emerge from an established downtrend — typically a 10-15% decline over at least 2-4 weeks before the first valley forms.

**2. Three Ascending Troughs**
- **Valley 1**: The lowest point of the pattern. Price declines and then bounces.
- **Valley 2**: Price pulls back but holds above Valley 1's low. Another bounce follows.
- **Valley 3**: Another pullback, but again holds above Valley 2. The final bounce launches the breakout.

Each valley should be clearly distinct, ideally separated by at least 10-15 bars on your chosen timeframe.

**3. Resistance Level (Neckline)**
A horizontal or slightly sloping resistance zone connects the peaks between the valleys. The breakout above this neckline confirms the pattern.

### Volume Confirmation
Volume ideally decreases across each successive valley (sellers exhausting) and surges on the breakout above the neckline.

---

## How to Trade the Three Rising Valleys

### Entry Strategy

**Conservative Entry**: Wait for a confirmed close above the neckline resistance on above-average volume. This reduces false breakout risk.

**Aggressive Entry**: Enter near Valley 3 when price shows clear support and candlestick reversal signals (e.g., hammer, bullish engulfing).

### Stop Loss Placement
- Just below Valley 3 (tightest stop, maximises R:R)
- Below Valley 2 as a wider stop for volatile instruments
- Below the lowest valley (Valley 1) for maximum safety

### Profit Targets

The classical measured move target:
1. Measure the height from Valley 1 to the neckline resistance
2. Project that distance upward from the breakout point

If Valley 1 is at $50 and the neckline is at $60 (height = $10), the target after breakout is $70.

