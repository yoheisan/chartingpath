## Risk Management Framework

- **Position size**: Risk no more than 1-2% of total trading capital per trade
- **Risk:Reward**: Target minimum 1:2 R:R; the measured move typically allows 1:3 or better
- **Partial profits**: Take 50% of position at the first target, let remainder run with a trailing stop
- **Pattern failure exit**: If price breaks back below Valley 3, exit without hesitation

