## Historical Performance

Studies of reversal patterns indicate that patterns with ascending lows tend to have higher success rates than flat triple bottoms:

- **Success rate**: 65-75% when confirmed with volume breakout
- **Average gain**: 18-25% above the neckline breakout in equity markets
- **Average time to target**: 4-8 weeks on daily chart setups

These figures improve substantially when combined with:
- RSI divergence (bullish divergence across the three valleys)
- MACD histogram improvement at each valley
- Price holding above a rising 20-day moving average

