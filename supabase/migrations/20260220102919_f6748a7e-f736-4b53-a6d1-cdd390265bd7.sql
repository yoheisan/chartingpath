## Combining with Technical Indicators

### RSI Divergence
One of the highest-probability setups occurs when RSI shows bullish divergence alongside the three valleys, confirming waning bearish momentum.

### MACD
Look for the MACD histogram to show progressively smaller negative bars at each valley, followed by a bullish crossover at or before the neckline breakout.

### Moving Averages
The best setups form when price oscillates around or just below a key moving average (50-day or 200-day). The neckline breakout often coincides with reclaiming this MA.

