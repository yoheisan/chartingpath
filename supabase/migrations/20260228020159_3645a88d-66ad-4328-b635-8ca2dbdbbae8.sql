## Trend Following Strategies

"The trend is your friend"—these strategies capitalize on established market momentum.

### 5. Classic Trend Following
**Level:** Intermediate | **Timeframe:** Weeks to months

Follow established trends until a clear reversal signal appears.

**Entry:** Enter when price breaks above 20-day high (long) or below 20-day low (short).
**Exit:** Trail stop using 10-day low (for longs) or 10-day high (for shorts).
**Notable Traders:** Ed Seykota, Richard Dennis (Turtle Traders)

📖 **Further Reading:** [Trend Following: Riding Market Momentum](/blog/trend-following-strategy) | [Trend Following: Aligning Patterns with the Dominant Trend](/blog/trend-following-pattern-alignment)

### 6. Breakout Trading
**Level:** Intermediate | **Timeframe:** Intraday to multi-day

Enter trades when price breaks out of support or resistance levels.

**Entry:** Enter on close above resistance with volume surge (50%+ above average).
**Exit:** Target measured move from breakout point.
**Notable Traders:** Jesse Livermore

📖 **Further Reading:** [Breakout Trading: Capturing Explosive Price Moves](/blog/breakout-trading-complete) | [Breakout Trading: Executing Chart Pattern Breakouts](/blog/breakout-pattern-execution)

