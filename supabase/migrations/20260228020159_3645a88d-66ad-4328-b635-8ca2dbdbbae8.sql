## Time-Based Trading Strategies

These strategies are defined primarily by how long you hold positions.

### 1. Buy and Hold Strategy
**Level:** Beginner | **Timeframe:** Years to decades | **Frequency:** 1–5 trades/year

A long-term investment approach where assets are purchased and held regardless of short-term fluctuations.

**Entry:** Buy fundamentally strong assets at fair or undervalued prices.
**Exit:** Rarely exit. Only sell when fundamentals deteriorate significantly.
**Notable Traders:** Warren Buffett, Charlie Munger, John Bogle

📖 **Further Reading:** [Buy and Hold Strategy: The Long-Term Wealth Builder](/blog/buy-and-hold-strategy)

### 2. Swing Trading
**Level:** Intermediate | **Timeframe:** Days to weeks | **Frequency:** 5–20 trades/month

A medium-term strategy that seeks to capture price movements over several days to weeks.

**Entry:** Buy at swing lows in uptrends, short at swing highs in downtrends.
**Exit:** Target the opposite end of the swing range. Trail stops as position moves.
**Notable Traders:** Paul Tudor Jones

📖 **Further Reading:** [Swing Trading Strategy](/blog/swing-trading-strategy) | [Swing Trading Patterns: Multi-Day Pattern Confirmation](/blog/swing-trading-pattern-strategies)

### 3. Day Trading
**Level:** Advanced | **Timeframe:** Minutes to hours | **Frequency:** 5–100 trades/day

Positions are opened and closed within the same trading day to avoid overnight risk.

**Entry:** Look for setups in the first 1-2 hours of market open.
**Exit:** All positions closed before market close.
**Notable Traders:** Ross Cameron, Andrew Aziz

📖 **Further Reading:** [Day Trading: Mastering Intraday Price Action](/blog/day-trading-strategy) | [Day Trading Patterns: Intraday Pattern Confirmation Guide](/blog/day-trading-pattern-strategies)

### 4. Scalping
**Level:** Expert | **Timeframe:** Seconds to minutes | **Frequency:** 10–200 trades/day

Ultra-short-term strategy aiming to profit from small price movements.

**Entry:** Enter on minor pullbacks using 1-minute charts.
**Exit:** Target 0.1%-0.5% per trade.

📖 **Further Reading:** [Scalping: High-Frequency Short-Term Trading](/blog/scalping-strategy) | [Scalping with Micro-Patterns](/blog/scalping-micro-pattern-confirmation)

