## Root Causes in Trading

### 1. Overtrading
The most common burnout trigger. Taking 50 trades per day when 5 quality setups exist forces constant decision-making without proportional reward.

**The Math of Overtrading:**
- 50 trades/day × 3 decisions each = 150 decisions
- Human optimal decisions/day ≈ 35 significant choices
- Result: Decision fatigue by lunch, poor trades in afternoon

### 2. Inadequate Rest Periods
Markets trade 24/5 (forex) or have pre-market/after-hours (stocks). The temptation to always be "on" destroys recovery.

### 3. Unrealistic Expectations
Expecting consistent daily profits creates chronic stress when reality—normal drawdowns, losing streaks—intervenes.

### 4. Poor Risk Management
Trading without stops or with excessive size means every position triggers maximum stress response.

### 5. Lack of Purpose Beyond Money
Traders motivated solely by P&L lack sustainable motivation. When losses occur, there''s nothing to fall back on.

