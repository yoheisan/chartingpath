## Recovery Protocol: From Burnout to Performance

### Phase 1: Complete Break (1-2 Weeks)
- Zero trading, zero market exposure
- Focus entirely on physical health (sleep, exercise, nutrition)
- Reconnect with family, friends, non-trading interests
- No trading books, podcasts, or community engagement

### Phase 2: Gradual Re-entry (2-4 Weeks)
- Paper trading only
- Maximum 2 hours screen time
- Trade only A+ setups (maybe 1-2 per day)
- Daily journaling: feelings first, then trade analysis

### Phase 3: Reduced Live Trading (4-8 Weeks)
- 25% normal position size
- Strict session limits (e.g., 2 hours max)
- Weekly performance reviews with accountability partner
- Immediate stop if burnout symptoms return

### Phase 4: Sustainable Practice (Ongoing)
- Build prevention strategies into permanent routine
- Regular check-ins on emotional state
- Quarterly trading "vacations" of 1-2 weeks
- Continuous adjustment of workload to capacity

