Trading burnout is a state of chronic stress that leads to physical, emotional, and mental exhaustion. It occurs when the demands of active trading exceed your capacity to cope, resulting in decreased performance, cynicism about markets, and a sense of ineffectiveness that can end promising trading careers.

### The Neuroscience of Burnout

When you trade, your brain processes thousands of micro-decisions involving risk assessment, pattern recognition, and emotional regulation. The prefrontal cortex—responsible for executive function—consumes enormous glucose when active. Extended trading sessions without proper recovery literally deplete the brain''s fuel supply.

**The Stress Response Cascade:**
- **Acute stress**: Cortisol spikes aid focus short-term
- **Chronic stress**: Sustained cortisol damages hippocampus (memory)
- **Burnout phase**: HPA axis dysfunction, cortisol irregularity
- **Result**: Impaired decision-making, emotional volatility

Research from the University of Zurich found that traders experiencing burnout showed 40% more activation in the amygdala (fear center) and 30% less prefrontal activity compared to well-rested peers—a neurological profile that guarantees poor trading decisions.

