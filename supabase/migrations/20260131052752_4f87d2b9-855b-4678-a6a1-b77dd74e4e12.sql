## Patience in Different Market Conditions

### Trending Markets
- **Patient approach**: Wait for pullbacks to enter with trend
- **Common mistake**: Chasing extended moves, entering at tops

### Range-Bound Markets
- **Patient approach**: Wait for tests of range extremes
- **Common mistake**: Trading mid-range "breakouts" that fail

### Volatile Markets
- **Patient approach**: Reduce size, wait for cleaner setups
- **Common mistake**: Normal position sizing in abnormal conditions

### Low Volatility Markets
- **Patient approach**: Accept fewer opportunities, trade smaller
- **Common mistake**: Forcing trades, expanding watchlist recklessly

