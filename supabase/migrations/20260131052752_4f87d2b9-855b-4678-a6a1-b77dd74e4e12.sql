## Types of Trading Patience

### 1. Pre-Trade Patience
Waiting for your setup to form completely before entering.

**Impatient behavior**:
- Entering "early" because pattern "looks like" it''s forming
- Buying before breakout confirms
- Shorting into uptrend expecting reversal

**Patient behavior**:
- Waiting for candle close confirmation
- Requiring volume expansion on breakouts
- Letting patterns complete fully before entry

### 2. In-Trade Patience
Allowing winners to run and giving trades room to work.

**Impatient behavior**:
- Taking profits at first sign of resistance
- Moving stops to breakeven immediately
- Closing trades before target reached

**Patient behavior**:
- Trailing stops according to plan
- Accepting temporary adverse movement
- Letting targets hit or stops trigger

### 3. Post-Trade Patience
Waiting for next quality setup rather than revenge trading or forcing action.

**Impatient behavior**:
- Immediately looking for next trade after loss
- Overtrading on winning days to "maximize"
- Entering suboptimal setups because "nothing else available"

**Patient behavior**:
- Stepping away after losses for recovery
- Accepting that some days have no setups
- Being satisfied with 1-2 quality trades daily

