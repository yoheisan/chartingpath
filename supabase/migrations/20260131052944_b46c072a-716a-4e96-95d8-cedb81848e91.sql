## Signs of Under-Confidence

### Behavioral Indicators
- **Hesitation**: Missing entries while waiting for "more confirmation"
- **Early exits**: Closing winners before targets from fear
- **Position shrinkage**: Trading too small relative to account
- **Setup avoidance**: Skipping valid setups because "not sure"
- **Seeking validation**: Asking others to confirm trades

### Psychological Indicators
- Constant second-guessing after entry
- Attributing wins to luck, losses to self
- Anxiety when positions are open
- Relief when trades close (even winners)
- Avoiding looking at P&L

### Performance Impact
Under-confidence costs you in subtle ways:
- Win rate may be good, but average win is small (early exits)
- Many setups identified but few traded
- Giving edge back through over-caution

