## Recovering Confidence After Losses

### The Confidence-Loss Spiral
Losses → Self-doubt → Hesitation → Poor execution → More losses → Deeper doubt

Breaking the spiral:

### Step 1: Separate Execution from Outcome
Ask: "Did I follow my rules?"
- If yes: Loss is acceptable, part of edge playing out
- If no: Focus on why rules were broken, not the loss itself

### Step 2: Statistical Perspective
Remind yourself:
- Even 60% win rate means 4 losses in 10 trades
- Losing streaks are mathematically normal
- One loss says nothing about next trade

Calculate: What''s your expected losing streak for your win rate?

| Win Rate | Expected Max Losing Streak (per 100 trades) |
|----------|---------------------------------------------|
| 50% | 7-8 losses in a row |
| 60% | 5-6 losses in a row |
| 70% | 3-4 losses in a row |

### Step 3: Reduce Size Temporarily
After confidence-shaking losses:
- Cut position size by 50%
- Focus on execution, not profits
- Rebuild with wins at smaller size
- Scale back up gradually as confidence returns

### Step 4: Return to Education
When in doubt, stop trading and study:
- Review what works in your strategy
- Backtest to remind yourself of edge
- Paper trade until confidence stabilizes

