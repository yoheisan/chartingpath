## Signs of Over-Confidence

### Behavioral Indicators
- **Oversizing**: Position sizes beyond risk parameters
- **Rule violations**: Ignoring stops, adding to losers
- **Frequency spike**: Trading more after wins
- **Setup expansion**: Taking lower-quality trades
- **Leverage abuse**: Using maximum available margin

### Psychological Indicators
- Feeling invincible after winning streaks
- Dismissing losing trades as flukes
- Ignoring risk management as unnecessary
- Believing you''ve "figured out" the market

### Performance Impact
Over-confidence leads to:
- Catastrophic single losses (oversized positions)
- Giving back winning months quickly
- Account blowups from leverage/sizing errors

The goal is calibrated confidence—accurate assessment of your actual edge and limitations.

