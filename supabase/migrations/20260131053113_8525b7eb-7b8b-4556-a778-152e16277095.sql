## Common Discipline Failures

### Failure 1: Moving Stops
**Scenario**: Trade goes against you, approaching stop. You move stop further away "to give it room."

**Why It Happens**: Loss aversion—the pain of loss feels twice as strong as pleasure of gain.

**Solution**:
- Set stops before entry, when emotionally neutral
- Visualize stop getting hit as normal business expense
- Use automation to remove your hands from the process

### Failure 2: Cutting Winners
**Scenario**: Trade goes for you, shows $300 profit. You close it for fear of it reversing. It runs to $1,200.

**Why It Happens**: Fear of giving back open profits feels like loss.

**Solution**:
- Predetermined targets or trailing stop rules
- Think in R-multiples, not dollars
- Review journal: How often do your trades reach target?

### Failure 3: Revenge Trading
**Scenario**: You take a loss. Immediately you look for another trade to make it back. You overtrade, compounding losses.

**Why It Happens**: Brain interprets loss as injury requiring immediate remedy.

**Solution**:
- Mandatory cooling period after losses
- Daily loss limit that halts trading
- Focus on process: "Did I follow rules?"—if yes, no problem

### Failure 4: FOMO Trading
**Scenario**: You see a stock running 20%. You weren''t positioned. You chase entry at extended levels. It reverses.

**Why It Happens**: Social comparison + scarcity mindset (this is the only opportunity).

**Solution**:
- Remember: There are 15,000+ stocks, infinite setups
- If you missed it, you missed it—no P&L impact
- Wait for pullback entry if thesis valid

