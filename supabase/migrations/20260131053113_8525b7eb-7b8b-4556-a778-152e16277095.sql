## Prevention Strategies

### 1. Pre-Commitment Rules

**Daily Loss Limits**
Establish before trading begins:
- Maximum daily loss: 2-3% of account
- When hit, trading stops for the day—no exceptions
- Write it down; tell someone; automate if possible

**Trade Frequency Limits**
- Maximum trades per day: ___ (e.g., 5)
- Consecutive losses before mandatory break: ___ (e.g., 2)

**Cooling Period Rules**
- After any loss: Minimum 15-minute break
- After 2 consecutive losses: 30-minute break
- After hitting 50% daily loss limit: 1-hour break or done for day

### 2. Environmental Controls

**Physical Barriers**
- Step away from computer
- Go to another room
- Go outside for a walk

**Technology Barriers**
- Close trading platform
- Set timer before allowed to reopen
- Use platform features to lock trading after loss limits

**Social Accountability**
- Text/call trading buddy after losses
- Post in trading community before next trade
- Have someone who can remind you of rules

### 3. Cognitive Restructuring

**Reframe the Loss**
| Revenge Thought | Rational Reframe |
|-----------------|------------------|
| "I have to make this back" | "This loss is a normal cost of business" |
| "The market owes me" | "The market owes me nothing" |
| "One more trade to recover" | "Each trade is independent" |
| "I can''t end the day negative" | "Days don''t matter; months and years do" |

**The Independent Trade Concept**
Drill this into your mind: Every trade is statistically independent. Your next trade has the same probability of success regardless of what happened on the last trade.

### 4. Emotional Regulation

**Immediate Interventions**
When you notice revenge urges:
1. **Name it**: "I notice I want to revenge trade"
2. **Breathe**: 5 deep breaths, 4 seconds in, 6 seconds out
3. **Body scan**: Where am I holding tension?
4. **Challenge**: "What would happen if I do nothing?"

**The 10-Minute Rule**
After any loss:
- Set a timer for 10 minutes
- During that time, do nothing trading-related
- After 10 minutes, assess: Am I calm enough to trade rationally?
- If no, add another 10 minutes

