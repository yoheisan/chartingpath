## The Emotional Discipline Framework

### Step 1: Awareness
You can''t control what you don''t notice. Develop emotional awareness through:

**Body Scanning**
Before and during trading, notice:
- Heart rate: Elevated? Racing?
- Breathing: Shallow? Rapid?
- Muscle tension: Shoulders? Jaw? Hands?
- Stomach: Tight? Nauseous?

Physical symptoms precede emotional hijacking.

**Thought Monitoring**
Notice thoughts like:
- "I have to make back that loss"
- "This can''t lose again"
- "Everyone else is making money"
- "One more trade won''t hurt"

**Emotion Labeling**
Simply naming emotions reduces their power:
- "I notice I''m feeling fear"
- "There''s that familiar greed sensation"
- "Hope is arising about this position"

### Step 2: Pause
When you notice emotional activation:
- Do not act immediately
- Take 3-5 deep breaths
- Step away from screen if needed
- Create space between stimulus and response

**The 10-Second Rule**
Before any non-planned action:
- Wait 10 seconds
- Ask: "Is this in my plan?"
- If no, don''t do it

### Step 3: Reframe
Change your interpretation:

| Emotional Thought | Disciplined Reframe |
|-------------------|---------------------|
| "I can''t take another loss" | "Losses are part of my edge" |
| "I need to make this back" | "Each trade is independent" |
| "This has to work" | "I''ll take what the market gives" |
| "I''m missing out" | "There''s always another setup" |

### Step 4: Act According to Plan
After awareness, pause, and reframe:
- Refer to your written trading plan
- Execute what the plan says
- Accept the outcome regardless

