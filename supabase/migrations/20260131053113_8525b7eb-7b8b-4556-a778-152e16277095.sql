Emotional discipline in trading is the ability to execute your trading plan regardless of how you feel. It''s not about eliminating emotions—that''s impossible and undesirable. It''s about preventing emotions from hijacking your decision-making process.

### Why Emotions Are Dangerous in Trading

The emotional brain (limbic system) evolved for survival in physical environments. Its responses are:
- **Fast**: Acts before conscious thought
- **Binary**: Fight or flight, no nuance
- **Short-term focused**: Immediate threat response

Markets require:
- **Slow, deliberate analysis**
- **Nuanced probability thinking**
- **Long-term edge exploitation**

Your emotional brain sees a losing position as a threat and triggers fight (revenge trading) or flight (premature exit). Neither serves your edge.

---

## The Primary Trading Emotions

### 1. Fear

**Manifestations**:
- Hesitating on valid entries
- Moving stops wider to avoid loss
- Cutting winners too early
- Avoiding trading after losses

**Root Cause**: Overweighting potential pain of loss

**Counter-Strategy**:
- Pre-accept the maximum loss (defined by stop)
- Focus on process, not outcome
- Normalize losses as business expenses

### 2. Greed

**Manifestations**:
- Oversizing positions
- Ignoring profit targets
- Adding to winners without system justification
- Trading more frequently on winning days

**Root Cause**: Overweighting potential pleasure of gain

**Counter-Strategy**:
- Strict position sizing rules (non-negotiable)
- Predetermined targets and trailing stops
- Session limits regardless of P&L

### 3. Hope

**Manifestations**:
- Holding losers past stops
- Adding to losing positions
- Waiting for trade to "come back"
- Ignoring exit signals

**Root Cause**: Refusal to accept reality

**Counter-Strategy**:
- Hard stops, preferably automated
- Accept that losses are final, not temporary
- "If in doubt, get out"

### 4. Regret

**Manifestations**:
- Chasing trades you missed
- Revenge trading after losses
- Changing systems after missing big moves
- Dwelling on past trades instead of present

**Root Cause**: Inability to accept past decisions

**Counter-Strategy**:
- Missed trades have no P&L impact
- Focus on next opportunity, not last one
- Journal regrets, then release them

