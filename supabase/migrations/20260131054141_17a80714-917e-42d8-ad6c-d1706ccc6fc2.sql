The rising wedge is a bearish reversal and continuation pattern characterized by two upward-sloping converging trendlines. Despite appearing bullish (rising price), this pattern typically resolves to the downside, making it a valuable tool for identifying potential short opportunities.

## Pattern Psychology

### Why Rising Wedges Are Bearish

The rising wedge represents weakening momentum despite rising prices:

**Momentum Deterioration:**
- Price makes higher highs but at a decreasing rate
- Rally attempts become progressively weaker
- Buyers are losing conviction
- Sellers are testing lower resistance on each swing

**Supply and Demand Dynamics:**
- Demand is present but declining
- Supply is increasing at higher prices
- The convergence shows exhaustion
- Eventually sellers overpower weakened buyers

### Formation Context

**As Reversal Pattern:**
- Appears after sustained uptrend
- Signals trend exhaustion
- Higher probability setup
- Often marks significant tops

**As Continuation Pattern:**
- Appears during downtrend as counter-trend rally
- Represents temporary pause before continuation
- Price wedges up then breaks down to resume trend

## Technical Anatomy

### Required Elements

**Upper Trendline (Resistance):**
- Connects at least 2 swing highs (3+ ideal)
- Slopes upward
- Should show clear angle

**Lower Trendline (Support):**
- Connects at least 2 swing lows (3+ ideal)
- Slopes upward more steeply than resistance
- Lines must converge (not parallel)

**Convergence:**
- Upper and lower lines approach each other
- Creates the "wedge" shape
- Pattern narrows toward apex

### Key Measurements

**Wedge Angle:**
- Both trendlines slope upward
- Support line steeper than resistance
- Typical angle difference: 10-30 degrees

**Pattern Duration:**
- Minimum: 3 weeks (daily charts)
- Typical: 3 weeks to 3 months
- Intraday: minimum 4-6 hours

**Breakout Timing:**
- Usually occurs 2/3 to 3/4 through pattern
- Breakdowns near apex less reliable

## Trading the Rising Wedge

### Entry Strategies

**Breakdown Entry (Primary):**
- Wait for close below rising support line
- Volume confirmation increases probability
- Enter on breakdown candle close or next bar

**Retest Entry (Lower Risk):**
- Wait for breakdown then retest of broken trendline
- Enter when price rejects trendline from below
- Better risk/reward but may miss fast moves

### Stop-Loss Placement

**Conservative Stop:**
- Above the most recent swing high
- Accounts for false breakdowns

**Aggressive Stop:**
- Above the breakdown candle high
- Tighter risk, higher failure rate

### Price Target Calculation

**Measured Move Method:**
1. Calculate height from widest point of wedge
2. Project downward from breakdown point

**Example:**
- Wedge high: $60
- Wedge low (widest point): $50
- Height = $10
- Breakdown at $55
- Target = $55 - $10 = $45

**Alternative Targets:**
- Prior swing lows
- Key support levels
- Moving average support

## Volume Analysis

**During Formation:**
- Volume often decreases as pattern develops
- Shows declining interest in rally

**At Breakdown:**
- Volume expansion confirms breakdown
- "Panic selling" volume validates pattern

## Success Statistics

**Breakdown Success Rate:**
- Downside breakdown: 72-76%
- Average decline: 15-25%

**Pattern Failure:**
- Upside breakout: 24-28%
- When upside occurs, move can be significant

## Common Mistakes

1. **Assuming All Rising = Bullish**: Rising wedge is bearish
2. **Entering Before Breakdown**: Wait for confirmation
3. **Ignoring Volume**: Volume validates breakdowns
4. **Confusing with Channel**: Lines must converge
5. **Trading Near Apex**: Late breakdowns less reliable

