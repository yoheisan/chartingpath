Wedge patterns are powerful reversal and continuation formations that consistently trap traders who misinterpret their directional bias. This comprehensive guide covers both rising and falling wedges, providing professional frameworks for identification and trading.

## Understanding Wedge Patterns

### What Makes Wedges Unique

Unlike triangles where one trendline is horizontal, wedge patterns have both trendlines sloping in the same direction. This creates a distinctive shape where:

- **Rising Wedge**: Both lines slope upward (bearish pattern)
- **Falling Wedge**: Both lines slope downward (bullish pattern)

The counter-intuitive nature of wedges—rising wedges being bearish and falling wedges being bullish—reflects the underlying momentum dynamics.

### Momentum Exhaustion Theory

Wedges identify momentum exhaustion:

**Rising Wedge Psychology:**
- Price rises but each successive high shows less momentum
- Bulls push higher but with diminishing conviction
- The narrowing range shows buyers losing steam
- Eventually exhaustion leads to breakdown

**Falling Wedge Psychology:**
- Price falls but each successive low shows less momentum
- Bears push lower but with diminishing conviction
- The narrowing range shows sellers losing steam
- Eventually exhaustion leads to breakout

## Technical Requirements

### Rising Wedge Criteria

**Trendline Construction:**
- Support: Connects 2+ swing lows, slopes upward
- Resistance: Connects 2+ swing highs, slopes upward
- Resistance less steep than support (key distinction)
- Lines must converge

**Pattern Characteristics:**
- Price makes higher highs and higher lows
- Range compresses as pattern develops
- Volume typically decreases
- Breakdown occurs before apex

### Falling Wedge Criteria

**Trendline Construction:**
- Resistance: Connects 2+ swing highs, slopes downward
- Support: Connects 2+ swing lows, slopes downward
- Support less steep than resistance (key distinction)
- Lines must converge

**Pattern Characteristics:**
- Price makes lower highs and lower lows
- Range compresses as pattern develops
- Volume typically decreases
- Breakout occurs before apex

### Pattern vs Channel Distinction

**Wedge:**
- Lines converge (approach each other)
- Creates pointed apex
- Range narrows over time

**Channel:**
- Lines are parallel
- No apex formation
- Range remains constant

## Context: Reversal vs Continuation

### Wedges as Reversal Patterns

**Rising Wedge Reversal:**
- Forms after sustained uptrend
- Marks distribution phase
- High-probability short setup
- Often precedes significant declines

**Falling Wedge Reversal:**
- Forms after sustained downtrend
- Marks accumulation phase
- High-probability long setup
- Often precedes significant rallies

### Wedges as Continuation Patterns

**Rising Wedge Continuation:**
- Forms during downtrend as counter-trend rally
- Bears pause as bulls attempt recovery
- Breakdown resumes prior downtrend

**Falling Wedge Continuation:**
- Forms during uptrend as counter-trend pullback
- Bulls pause as bears attempt correction
- Breakout resumes prior uptrend

## Trading Strategies

### Rising Wedge Trading

**Entry (Short):**
