Strip away every indicator. Remove the moving averages, oscillators, and fancy overlays. What remains is price action—the purest form of technical analysis and the foundation upon which all other analysis is built.

### The Philosophy of Price Action

Price action trading is based on a fundamental truth: price is the final arbiter. Every piece of information—fundamental, technical, sentiment—ultimately expresses itself through price movement. By reading price directly, you see the market''s true state without the lag of indicators.

### The Language of Candles

Each candlestick tells a story of the battle between buyers and sellers:

**The Body**: The distance between open and close shows who won the session. Long bodies mean decisive victory; short bodies mean stalemate.

**The Wicks (Shadows)**: The rejection from highs or lows shows where one side overwhelmed the other. Long wicks indicate failed attempts and rejection.

**The Close**: Where price closes relative to the range reveals the session''s bias. Closing in the upper third is bullish; lower third is bearish.

### Key Price Action Concepts

#### Support and Resistance

The most fundamental concept—levels where price has previously reversed:

**Support**: A level where buying pressure has historically overcome selling pressure, causing price to bounce.

**Resistance**: A level where selling pressure has historically overcome buying pressure, causing price to fall.

**Role Reversal**: When support breaks, it often becomes resistance (and vice versa). This is one of the most reliable price action principles.

#### Trend Structure

Price action defines trend through swing points:

**Uptrend**: Higher highs and higher lows
**Downtrend**: Lower highs and lower lows
**Range**: Roughly equal highs and lows

The trend continues until structure breaks. A lower low in an uptrend or a higher high in a downtrend signals potential reversal.

#### Momentum and Character

Price action reveals momentum through:

**Candle Size**: Increasing candle sizes suggest building momentum; decreasing sizes suggest exhaustion.

**Thrust vs. Consolidation**: Strong moves followed by tight consolidation suggest continuation. Strong moves followed by larger counter-moves suggest reversal.

**Time at Price**: How long price spends at a level indicates acceptance or rejection.

### Price Action Patterns

#### Single Candle Patterns

**Pin Bar (Hammer/Shooting Star)**: Long wick, small body. Shows rejection of a price level. Bullish when wick is below (hammer); bearish when wick is above (shooting star).

**Engulfing**: Current candle completely engulfs previous candle''s body. Strong reversal signal, especially at key levels.

**Doji**: Open equals close. Indecision that often precedes reversals.

**Marubozu**: No wicks—open equals low (bullish) or open equals high (bearish). Strong conviction signal.

#### Multiple Candle Patterns

**Inside Bar**: Current bar''s range is entirely within the previous bar''s range. Consolidation before expansion, often a continuation pattern.

**Outside Bar**: Current bar''s range engulfs previous bar''s entire range. Strong reversal or continuation depending on context.

**Fakey (False Break)**: Inside bar followed by a false breakout and reversal. Powerful signal when it traps traders.

### Context Is Everything

The same pattern has different implications depending on context:

**Pin Bar at Major Resistance**: High probability reversal signal
**Pin Bar in Middle of Range**: Lower probability, likely noise

**Engulfing After Extended Move**: Potential reversal
**Engulfing at Beginning of Move**: Continuation signal

Always ask: Where is this pattern occurring? What is the larger structure?

### Reading Price Action in Real-Time

#### The Three-Act Structure

Every move has three phases:

**Act 1 - Impulse**: Strong, directional movement with large candles
**Act 2 - Correction**: Counter-trend movement with smaller, overlapping candles
**Act 3 - Continuation**: Resumption of the impulse direction

Trade with Act 1 and Act 3. Avoid trading Act 2.

#### Signs of Strength

- Large candles closing at highs/lows
- Little overlap between candles
- Quick rejection of support/resistance tests
- Shallow pullbacks

#### Signs of Weakness

- Decreasing candle size
- Increasing overlap between candles
- Failed breakouts
- Deep pullbacks into the range

### Price Action Trading Strategies

#### Strategy 1: Support/Resistance Bounce

Trade reactions at key levels:

**Setup**: Price approaches significant support or resistance

**Entry Trigger**: Reversal candle (pin bar, engulfing) at the level
**Stop Loss**: Beyond the level and reversal candle wick
**Target**: Previous swing high/low or next key level

#### Strategy 2: Break and Retest

Trade after level breaks and price retests:

**Setup**: Price breaks through support/resistance, then returns to test it

**Entry Trigger**: Rejection candle at the broken level (now acting as opposite)
**Stop Loss**: Beyond the retest extreme
**Target**: Measured move or next key level

#### Strategy 3: Trend Continuation

Trade pullbacks in trending markets:

**Setup**: Established trend with clear structure

**Entry Trigger**: Pullback to support (uptrend) or resistance (downtrend) with reversal candle
**Stop Loss**: Beyond the pullback extreme
**Target**: New high (uptrend) or new low (downtrend)

### Advanced Price Action Concepts

#### Order Flow Footprint

Price action reflects order flow:
- Absorption: Large volume at a level but price doesn''t move—someone is absorbing orders
- Exhaustion: Large move on decreasing volume—buyers/sellers running out
- Initiation: Breakout on increasing volume—new participants entering

#### Time-Based Price Action

Different sessions have different characteristics:
- London open often reverses Asian session moves
- New York open often creates daily high or low
- End of day may see profit-taking

