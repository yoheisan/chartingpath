While most traders watch price charts, professional traders watch order flow—the actual buying and selling activity that creates those price movements. Understanding order flow gives you insight into what large players are doing in real-time.

### What Is Order Flow?

Order flow is the stream of buy and sell orders entering the market. Every price movement is caused by orders:

**Aggressive Orders (Market Orders)**: Traders willing to pay the current asking price to buy immediately, or accept the current bid to sell immediately. These create immediate price impact.

**Passive Orders (Limit Orders)**: Traders willing to wait for price to come to them. These create the liquidity that aggressive orders trade against.

**The Tape**: The time and sales feed showing each executed trade—the historical record of order flow.

### The Order Book (Level 2)

The order book shows pending limit orders at each price level:

**Bid Side**: Buyers waiting to purchase at various prices below current price
**Ask Side**: Sellers waiting to sell at various prices above current price
**Spread**: The gap between best bid and best ask

**Reading the Book**:
- Thick bid side (many buyers waiting): Potential support
- Thick ask side (many sellers waiting): Potential resistance
- Thin order book: Prices can move quickly through levels

**Limitations**: Visible orders can be pulled (cancelled) before execution. Many orders are hidden (iceberg orders) or don''t show in the book (dark pools).

### Footprint Charts

Footprint charts show volume traded at each price, split by aggressive buyers vs. aggressive sellers:

**Delta**: The difference between buying volume and selling volume at each price level
- Positive delta: More aggressive buyers
- Negative delta: More aggressive sellers
- Delta divergence: Delta direction differs from price direction—potential reversal signal

**Point of Control (POC)**: The price with highest volume—where the most agreement occurred

**Value Area**: The range containing 70% of volume—the zone of fair value

### Reading Order Flow Signals

#### Absorption

Large volume at a price level that doesn''t move price:

**Bullish Absorption**: Sellers hitting the bid aggressively, but price doesn''t drop. Large buyer is absorbing all selling.

**Bearish Absorption**: Buyers lifting the offer aggressively, but price doesn''t rise. Large seller is absorbing all buying.

**Implication**: The absorbing party is likely accumulating or distributing a large position. Expect price to eventually move in their direction.

#### Exhaustion

Strong directional move that suddenly slows:

**Buying Exhaustion**: Large volume spike on up candle, but next candle is weak. Buyers used up; no more demand.

**Selling Exhaustion**: Large volume spike on down candle, but selling doesn''t continue. Sellers exhausted; potential bottom.

#### Imbalance

Extreme disparity between buying and selling:

**Stacked Imbalances**: Multiple consecutive prices showing 3:1 or greater imbalance in one direction. Signals strong conviction and potential continuation.

**Diagonal Imbalances**: Imbalances forming diagonally, suggesting aggressive pursuit of price.

### Practical Order Flow Analysis

#### The Time and Sales Tape

The tape shows every executed trade. Reading it reveals:

**Block Trades**: Large orders (often 1000+ shares) that signal institutional activity
**Sweep Orders**: Multiple fills at successive prices, suggesting urgency
**Print Patterns**: Clusters of same-size orders may be algorithm activity

#### Volume Profile Integration

Combine order flow with volume profile:

**High Volume Nodes**: Areas where significant business was done—likely to attract price again
**Low Volume Nodes**: Air pockets price can move through quickly
**Point of Control**: Strong magnet for price, especially for mean reversion

#### Delta Analysis

Track cumulative delta over time:

**Rising Cumulative Delta + Rising Price**: Confirmed uptrend, buyers in control
**Rising Cumulative Delta + Falling Price**: Divergence—buyers absorbing, potential reversal up
**Falling Cumulative Delta + Rising Price**: Divergence—sellers absorbing, potential reversal down
**Falling Cumulative Delta + Falling Price**: Confirmed downtrend, sellers in control

### Order Flow Trading Strategies

#### Strategy 1: Absorption Trade

Trade after identifying absorption at key levels:

**Setup**: Price at support/resistance, large volume but price doesn''t break through

**Entry**: After absorption confirms (price reverses from level)
**Stop Loss**: Beyond the absorption zone
**Target**: Previous swing or opposite boundary

#### Strategy 2: Delta Divergence

Trade when delta and price disagree:

**Setup**: Price makes new high/low but delta doesn''t confirm

**Entry**: After divergence appears and price shows reversal candle
**Stop Loss**: Beyond the extreme
**Target**: Mean reversion to previous structure

#### Strategy 3: Imbalance Continuation

Trade in the direction of stacked imbalances:

**Setup**: Multiple consecutive imbalance levels in same direction

**Entry**: On pullback toward imbalance zone
**Stop Loss**: Other side of imbalance zone
**Target**: Extension of the move

