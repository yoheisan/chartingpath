**Pattern**: Small-cap stocks tend to outperform large-caps in January.

**Statistics**: Small-caps have outperformed large-caps in January roughly 70% of the time historically.

**Why It Exists**: Tax-loss selling in December depresses small-cap prices. January brings rebalancing and fresh capital.

**How to Trade It**: Consider rotating to small-cap exposure (IWM, IJR) in late December.

