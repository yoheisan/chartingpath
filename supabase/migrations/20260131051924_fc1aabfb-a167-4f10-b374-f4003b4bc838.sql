**Pattern**: Markets tend to underperform from May through October compared to November through April.

**Statistics**: Since 1950, the S&P 500 has returned an average of 7% from November to April versus just 2% from May to October.

**Why It Exists**: Summer vacation periods reduce trading activity and institutional attention. Portfolio managers become more defensive ahead of year-end evaluation.

**How to Trade It**: Reduce equity exposure or move to defensive sectors in May. Increase equity exposure in November.

