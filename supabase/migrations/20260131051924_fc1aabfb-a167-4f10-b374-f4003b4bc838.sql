**Time and Sales**: Trade tape (standard with most platforms)
**Footprint Charts**: Volume at price with bid/ask split (specialized platforms: Sierra Chart, Bookmap, Jigsaw)
**Depth of Market (DOM)**: Ladder showing order book with execution capability

