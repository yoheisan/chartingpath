**Pattern**: The last five trading days of December and first two of January tend to be bullish.

**Statistics**: This seven-day period has been positive 79% of the time since 1969, with average gains of 1.3%.

**Why It Exists**: Tax-loss selling ends, holiday optimism prevails, low volume allows prices to drift higher.

**How to Trade It**: Look for long opportunities in late December, especially after any tax-loss selling pressure.

